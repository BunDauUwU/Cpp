//**Struct**//
struct Trie{
	ll cur;
	struct node{
		ll val, cnt;
		ll child[5];
	}Node[100006];
	void build(){
		cur = 0;
		memset(Node[cur].child, -1, sizeof(Node[cur].child));
		Node[cur].cnt = Node[cur].val = 0;
	}
	ll new_node(){
		cur++;
		memset(Node[cur].child, -1, sizeof(Node[cur].child));
		Node[cur].cnt = Node[cur].val = 0;
		return cur;
	}
	void add(string s){
		ll pos = 0, idx = 0;
		for(int i = 0; i<s.size(); i++){
			idx = s[i] - '0';
			if(Node[pos].child[idx] == -1){
				Node[pos].child[idx] = new_node();
			}
			pos = Node[pos].child[idx];
			Node[pos].cnt++;
		}
		Node[pos].val++;
	}
	ll find_s(string s){
		ll res = 0;
		ll pos = 0, idx = 0;
		for(int i = 0; i<s.size(); i++){
			idx = s[i] - '0';
			if(Node[pos].child[idx] == -1){
				Node[pos].child[idx] = new_node();
			}
			if(Node[pos].child[idx] != -1){
				pos = Node[pos].child[idx];
				res += Node[pos].val;
			}
		}
		res = res + Node[pos].cnt - Node[pos].val;
		return res;
	}
};
