#include <bits/stdc++.h>
#define ll long long
#define ld long double 
#define inf 0x3f3f3f3f
#define FILE "Typing" 
using namespace std;
//**Variable**//
ll t;
ll x;
string s;
ll MOD = 1e9+7;
ll A[5000006];
ll pre[5000006];
//**Struct**//

//**Function**//

int main()
{
    ios_base::sync_with_stdio(false);cin.tie(NULL);
    // freopen(FILE".inp","w",stdout);
    freopen(FILE".inp","r",stdin);
    freopen(FILE".out","w",stdout);
    cin>>t;
    while(t--){
    	cin>>x>>s;
    	ll n = s.size();
    	if(x==0){
			ll ans = 1e18;
			ll cnt = 0;
			bool hand = 0; // 0 -> tay trai
			for(int i = 0; i<n; i++){
				if(s[i] == 'T')continue;
				if(s[i] == 'A' && hand){
					hand=1-hand;
					cnt++;
				}
				if(s[i] == 'I' && !hand){
					hand=1-hand;
					cnt++;
				}
			}    
			ans = min(ans,cnt);
			cnt = 0;
			hand = 1;	
			for(int i = 0; i<n; i++){
				if(s[i] == 'T')continue;
				if(s[i] == 'A' && hand){
					hand=1-hand;
					cnt++;
				}
				if(s[i] == 'I' && !hand){
					hand=1-hand;
					cnt++;
				}
			}
			ans = min(ans, cnt);
			cout<<ans%MOD<<"\n";   	
    	}else{
   			memset(A, 0, sizeof(A));
   			memset(pre,0, sizeof(pre));
    		ll ans = 0;
       		ll cnt = 0;
       		bool hand = 0;
    		for(int i = 0; i<n; i++){
    			while(s[i] == 'T'){
    				A[i]++;
    				s.erase(i,1);
    			}
     		}
     		n = s.size();
    		for(int i = 0; i<n; i++){
    			if(s[i] != 'T'){
    				if(s[i] == 'A') hand = 0;
    				else hand = 1;
    				break;
    			}
    		}
     		cnt = 0;
    		for(int i = 0; i<n; i++){
				if(s[i] == 'T')continue;
				if(s[i] == 'A' && hand){
					hand=1-hand;
					cnt++;
				}
				if(s[i] == 'I' && !hand){
					hand=1-hand;
					cnt++;
				}
				pre[i] = cnt;
			}
			ll tmp = 0;
			ll tmpp = 0;
			ll tmppp = 0;
			for(int i = 0; i<n; i++){
				tmp = (tmp+A[i]+1)%MOD;
				tmpp = (tmpp + A[i]*pre[i])%MOD;
				tmppp = (tmppp+pre[i])%MOD;
				ans = ( ans +pre[i]*tmp - tmpp - tmppp)%MOD; 
			}
			cout<<ans%MOD<<"\n";
    	}
    }
    
    return 0;
}