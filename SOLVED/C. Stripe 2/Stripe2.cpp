using namespace std;
//**Struct**//

//**Variable**//
int n;
ll A[100005];
ll cnt[100005];
ll s=0;
//**Function**//
int main()
{
    ios_base::sync_with_stdio(false);cin.tie(NULL);
    // freopen(FILE".inp","w",stdout);
    freopen(FILE".inp","r",stdin);
    freopen(FILE".out","w",stdout);
    cin>>n;
    if(n<3){
    	cout<<0;
    	return 0;
    }
    for(int i = 1; i<=n; i++){
    	cin>>A[i];
    	s+=A[i];
    }
    if(s%3!=0){
    	cout<<0;
    	return 0;
    }
    ll tg = s/3;
    ll ss = 0;
    for(int i = n; i>=1; i--){
    	ss+=A[i];
    	if(ss==tg){
    		cnt[i]=1;
    	}
    }
    for(int i = n-1; i>=1; i--){
    	cnt[i]+=cnt[i+1];
    }
    ll ans = 0;
    ss= 0;
    for(int i = 1; i<= n-2; i++){
    	ss+=A[i];
    	if(ss==tg){
    		ans+=cnt[i+2];
    	}
    }
    cout<<ans;
    return 0;
}
