#include <bits/stdc++.h>
#define ll long long
#define ld long double 
#define inf 0x3f3f3f3f
#define FILE "XOR" 
using namespace std;
//**Variable**//
int  n, q, x;
ll A[1000006];
ll st = 0;
string s;
//**Struct**//
struct Trie{
	struct Node{
		int sz;
		Node* child[2];
		Node(){
			child[0] = child[1] = NULL;
			sz = 0;
		}
	};
	Node *root;
	int tmp[1000006];
	void build(){
		root = new Node();
	}
	int SIZE(Node *x) { return x ? x->sz : 0; }
	void add(Node * pos, int x, int h){
		int idx = (x>>h) & 1;
		if(pos -> child[idx] == NULL) pos -> child[idx] = new Node();
		if(h > 0){
			add(pos -> child[idx], x, h-1);
		}
		else{
			pos -> child[idx] -> sz++;
		}
		pos -> sz = SIZE(pos -> child[0])  + SIZE(pos -> child[1]);
	}
	void XOR(int x){
		for(int i = 30; i>=0; i--){
			if((x>>i & 1)){
				tmp[i]^=1;
			}
		}
	}
	int find(Node *pos, int k, int h){
		if(h < 0) return 0;
		int nx = SIZE(pos -> child[tmp[h]]);
		if(k<=nx)return find(pos -> child[tmp[h]], k, h-1);
		return find(pos -> child[!tmp[h]], k - nx, h-1) + (1<<h); 
	}
}T;
//**Function**//

int main()
{
    ios_base::sync_with_stdio(false);cin.tie(NULL);
    // freopen(FILE".inp","w",stdout);
    freopen(FILE".inp","r",stdin);
    freopen(FILE".out","w",stdout);
    cin>>n>>q;
    T.build();
    for(int i = 1; i<=n; i++){
    	cin>>A[i];
    	T.add(T.root, A[i], 30);
    }
    while(q--){
    	cin >> s >> x;
    	if(s=="FIND"){
    		cout<< T.find(T.root, n - x + 1, 30)<<"\n";
    	}else{
    		T.XOR(x);
    	}
    }
    return 0;
}
