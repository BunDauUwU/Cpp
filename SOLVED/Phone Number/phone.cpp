//**Struct**//
struct Trie{
	ll cur;
	struct node{
		ll cnt, val;
		ll child[101];
	}Node[100001];
	void build(){
        cur = 0;
        memset(Node[0].child,-1,sizeof(Node[0].child));
        Node[0].cnt = Node[0].val = 0;
    }
	ll new_node(){
		cur++;
		memset(Node[cur].child,-1,sizeof(Node[cur].child));
		Node[cur].cnt = Node[cur].val = 0;
		return cur;
	}
	bool add(string s){
		ll pos = 0;
		ll idx = 0;
		for(int i = 0; i<s.size(); i++){
			idx = s[i] - '0';
			if(Node[pos].child[idx] == -1){
				Node[pos].child[idx] = new_node();
			}
			pos = Node[pos].child[idx];
			Node[pos].cnt++;
			if(Node[pos].cnt >= 2 && Node[pos].val){
				return false;
			}
		}
		Node[pos].val++;
		return true;
	}
};
//**Variable**//
int t;
int n;
string s;

//**Function**//
bool cmp(string a, string b){
	return a.size() < b.size();
}
int main()
{
    ios_base::sync_with_stdio(false);cin.tie(NULL);
    // freopen(FILE".inp","w",stdout);
    freopen(FILE".inp","r",stdin);
    freopen(FILE".out","w",stdout);
    cin>>t;
    while(t--){
    	vector<string> ss;
    	Trie T; 
    	T.build();
    	cin>>n;
    	bool check = 1;
    	for(int i = 1; i<=n; i++){
    		cin>>s;
    		ss.push_back(s);
    	}
    	sort(ss.begin(),ss.end(),cmp);
    	for(auto it : ss){
    		// cout<<it<<"\n";
    		if(!T.add(it)){
    			check = 0;
    		}
    	}
    	if(check){
    		cout<<"YES\n";
    	}else{
    		cout<<"NO\n";
    	}
    }
    return 0;
}
