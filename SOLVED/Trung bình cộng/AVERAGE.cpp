ll solve(ll n,ll k){
    unordered_map<ll, ll> m;
    ll sum = 0, maxLen = 0;
    for (int i = 1; i <=n; i++) {
        sum += A[i];
        if (sum == k)
            maxLen = i + 1;

        if (m.find(sum) == m.end())
            m[sum] = i;
 
        if (m.find(sum - k) != m.end()) {
            if (maxLen < (i - m[sum - k]))
                maxLen = i - m[sum - k];
        }
    } 
    return maxLen;
}
