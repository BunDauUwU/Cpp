#include <bits/stdc++.h>
#define ll long long
#define ld long double 
#define inf 0x3f3f3f3f
#define fi first
#define se second
#define FILE "VISIT" 
using namespace std;
//**Variable**//
ll n, B;
pair<ll,ll> p[1000006];
//**Struct**//

//**Function**//
bool cmp(pair<ll,ll> a, pair<ll,ll> b){
	return a.fi < b.fi;
}
int main()
{
    ios_base::sync_with_stdio(false);cin.tie(NULL);
    // freopen(FILE".inp","w",stdout);
    freopen(FILE".inp","r",stdin);
    freopen(FILE".out","w",stdout);
    cin>>n>>B;
    for(int i = 1; i<=n; i++){
    	cin>>p[i].fi>>p[i].se;
    }
    sort(p+1, p+1+n);
    int ans = 0;
    for(int i = 1; i<=n; i++){
    	if(B - p[i].fi > 0){
    		if(B/p[i].fi >= p[i].se){
    			ans += p[i].se;
    			B -= p[i].se*p[i].fi;
    			p[i].se = 0;
    		}
    		else{
    			ans += B/p[i].fi;
    			B -= B/p[i].fi*p[i].fi;
    			p[i].se -= B/p[i].fi;
    		}
    	}else{
    		break;
    	}
    }
    cout<<ans;
    return 0;
}
