#include <bits/stdc++.h>
#define ll long long
#define ld long double 
#define inf 0x3f3f3f3f
#define FILE "cotco" 
#define TOP 0
#define RIGHT 1
#define BOT 2
#define LEFT 3
using namespace std;
//**Variable**//
int h, w;
char s[1001][1001];
bool circle[1001][1001];
bool cmp[1001][1001][4];
ld dp[1001][1001][4];
ld pi = acos(-1.0);
ld fourth = 10*pi / 4.0; 
//**Struct**//

//**Function**//
ld solve(int x, int y, int d){
    if(cmp[x][y][d]){
        return dp[x][y][d];
    }
    if(x == h-1 && y == w-1 && ( d == RIGHT || d == BOT )){
        return 5;
    }
    ld cur = 1000000000000000.0;
    if(d==TOP){
        if(y+1<w)cur = min(cur, 10.0 + solve(x,y+1,TOP));
        cur = min(cur,solve(x,y,RIGHT)+(circle[x][y]?fourth:10.0));
    }
    if(d==RIGHT){
        if(x+1<h)cur = min(cur, 10.0 + solve(x+1,y,RIGHT));
        if(y+1<w)cur = min(cur,solve(x,y+1,BOT)+(circle[x][y+1]?fourth:10.0));
    }
    if(d==BOT){
        if(x+1<h)cur = min(cur,solve(x+1,y,RIGHT)+(circle[x+1][y]?fourth:10.0));
        if(y+1<w)cur = min(cur, 10.0 + solve(x,y+1,BOT));
    }
    if(d==LEFT){
        if(x+1<h)cur = min(cur, 10.0 + solve(x+1,y,LEFT));
        cur = min(cur,solve(x,y,BOT)+(circle[x][y]?fourth:10));
    }
    cmp[x][y][d] = 1;
    return dp[x][y][d] = cur;
}
int main()
{
    ios_base::sync_with_stdio(false);cin.tie(NULL);
    // freopen(FILE".inp","w",stdout);
    freopen(FILE".inp","r",stdin);
    freopen(FILE".out","w",stdout);
    cin>>h>>w;
    for(int i = 0; i<h; i++){
        for(int j = 0; j<w; j++){
            cin>>s[i][j];
            circle[i][j] = (s[i][j] == 'O');
        }
    }   
    cout<<fixed<< setprecision(12) << 5.0 + min(solve(0,0,TOP), solve(0,0,LEFT));
    return 0;
}
