//**Struct**//
struct Trie{
	ll cur ;
	ll ans ;
	ll res ;
	struct node{
		ll cnt,val;
		char c;
		ll child[26];
	}Node[10006];
	void build(){
		cur = 0;
		memset(Node[cur].child,-1,sizeof(Node[cur].child));
		Node[cur].val = Node[cur].cnt = 0;
	}
	ll new_node(){
		cur++;
		memset(Node[cur].child,-1,sizeof(Node[cur].child));
		Node[cur].val = Node[cur].cnt = 0;
		return cur;
	}
	void add(string s){
		int pos = 0, idx = 0;
		for(int i = 0; i<s.size(); i++){
			idx = s[i] - 'a';
			if(Node[pos].child[idx] == -1){
				Node[pos].child[idx] = new_node();
			}
			pos = Node[pos].child[idx];
			Node[pos].cnt++;
			Node[pos].c = s[i];
			ans+=Node[pos].val;
		}
		Node[pos].val++;
	}
	
};
//**Variable**//
int n;
string s;
vector<string>ss;
//**Function**//
int main()
{
    ios_base::sync_with_stdio(false);cin.tie(NULL);
    // freopen(FILE".inp","w",stdout);
    freopen(FILE".inp","r",stdin);
    freopen(FILE".out","w",stdout);
    cin>>n;
    Trie T;
    T.build();
    for(int i = 1; i<=n; i++){
    	cin>>s;
    	ss.push_back(s);
    }
   	sort(ss.begin(),ss.end());
   	for(auto it : ss){
   		T.add(it);
   	}
    cout<<T.ans;
    return 0;
}
