//**Struct**//
struct Trie{
	ll cur;
	struct node{
		ll val, cnt;
		ll child[26];
	}Node[10006];
	Trie(){
		cur = 0;
		memset(Node[cur].child, -1, sizeof(Node[cur].child));
		Node[cur].cnt = Node[cur].val = 0;
	}
	ll new_node(){
		cur++;
		memset(Node[cur].child, -1, sizeof(Node[cur].child));
		Node[cur].cnt = Node[cur].val = 0;
		return cur;
	}
	void add(string s){
		ll pos = 0, idx;
		for(int i = 0; i<s.size(); i++){
			idx = s[i] - 'a';
			if(Node[pos].child[idx] == -1){
				Node[pos].child[idx] = new_node();
			}
			pos = Node[pos].child[idx];
			Node[pos].cnt++;
		}
		Node[pos].val++;
	}
	ll dfs(ll pos, ll ss){
		ll res = ss;
		for(int i = 0; i<26; i++){
			if(Node[pos].child[i] != -1){
				if(Node[pos].val){
					res = max(res,dfs(Node[pos].child[i],ss+1));
				}else{
					res = max(res,dfs(Node[pos].child[i],ss));
				}
			}
		}
		return res;
	}
};
//**Variable**//
int n;
string s;
