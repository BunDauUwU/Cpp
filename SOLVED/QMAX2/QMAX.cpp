//**Struct**//
struct ITree{
	struct Nodes{
		ll val,lz;
	}T[4*N];
	void lazy(int id,int l,int r){
		if(T[id].lz!=0){
			int tmp = T[id].lz;
			T[id].val+=tmp;
			if(l<r){
				T[id*2].lz += tmp;
				T[id*2+1].lz += tmp;
			}
			T[id].lz = 0;
		}
	}
	void update(int id,int l,int r,int u,int v,ll val){
		lazy(id,l,r);
		if( v<l || r<u ) return ;
		if( u<=l && r<=v ) {
			T[id].val += val;
			if(l<r){
				T[id*2].lz += val;
				T[id*2+1].lz += val;
			}
			return ;
		}
		int mid = (l+r)/2;
		update(id*2,l,mid,u,v,val);
		update(id*2+1,mid+1,r,u,v,val);
		T[id].val = max(T[id*2].val,T[id*2+1].val);
	}
	ll get(int id,int l,int r,int u,int v){
		lazy(id,l,r);
		if(v<l || r<u) return 0;
		if(u<=l && r<=v){
			return T[id].val;
		}
		int mid = (l+r)/2;
		return max(get(id*2,l,mid,u,v),get(id*2+1,mid+1,r,u,v));
	}
};
