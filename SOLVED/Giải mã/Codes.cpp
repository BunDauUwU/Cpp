using namespace std;
//**Variable**//
string s;
//**Struct**//
struct Trie{
	struct Node{
		int cnt, val;
		Node* child[2];
		Node(){
			cnt = val = 0;
			child[0] = child[1] = NULL;
		}
	};
	Node* root;
	void build(){
		root = new Node();
	}
	void reset(Node *pos){
		for(int i = 0; i<2; i++){
			if(pos -> child[i]){
				reset(pos -> child[i]);
			}
		}
		delete(pos);
	}
	bool add(string s){
		Node* pos = root;
		int idx = 0;
		for(int i = 0; i<s.size(); i++){
			idx = (s[i] == '0') ? 0 : 1;
			if(pos -> child[idx] == NULL){
				pos -> child[idx] = new Node();
			}
			if(pos -> val >= 1){
				return false;
			}
			pos = pos -> child[idx];
			pos -> cnt++;
		}
		if(pos -> val >= 1){
				return false;
		}
		pos -> val++;
		return true;
	}
};
//**Function**//
bool cmp(string a, string b){
	return a.size()<b.size();
}
int main()
{
    ios_base::sync_with_stdio(false);cin.tie(NULL);
    // freopen(FILE".inp","w",stdout);
    // freopen(FILE".inp","r",stdin);
    // freopen(FILE".out","w",stdout);
    Trie T;
    T.build();
    vector<string> ss;
    bool yes = 1;
    while(cin>>s){
    	if(s == "9"){
    		sort(ss.begin(),ss.end(),cmp);
    		for(auto it : ss){
    			yes&=T.add(it);
    		}
    		ss.clear();
    		if(yes){
    			cout<<"YES\n";
    		}else{
    			cout<<"NO\n";
    		}
    		T.reset(T.root);
    		T.build();
    		yes = 1;
    		continue;
    	}
    	ss.push_back(s);
    }
    
    return 0;
}