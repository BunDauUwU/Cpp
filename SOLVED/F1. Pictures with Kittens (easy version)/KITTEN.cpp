//**Struct**//

//**Variable**//
ll n,K,X;
ll A[201];
ll dp[201][201];
ll len[201][201];
//**Function**//

int main()
{
    ios_base::sync_with_stdio(false);cin.tie(NULL);
    // freopen(FILE".inp","w",stdout);
    freopen(FILE".inp","r",stdin);
    freopen(FILE".out","w",stdout);
	cin>>n>>K>>X;
    for(ll i = 0 ; i < n; i++){
        cin >> A[i]; 
    }
    for(ll i = 0; i <= n; i++){
        for(ll j = 0; j<=n; j++){
            dp[i][j] = -1e18;
        }
    }
    ll ans = -1;
    dp[0][X] = 0;
    for(ll i = 1; i<=n; i++){
        for(ll j = 0; j < X; j++){
            for(ll k = 1; k<=K; k++){
                if(i - k < 0)break;
                if(dp[i-k][j+1] == -1e18){
                    continue;
                }
                dp[i][j] = max(dp[i][j], dp[i-k][j+1] + A[i-1]);
            }
        }
    }
    for(int i = n; i>= n-K+1; i--){
        for(ll j = 0; j <= X; j++)
            ans = max(ans, dp[i][j]);
    }
    cout<<ans;
    return 0;
}














