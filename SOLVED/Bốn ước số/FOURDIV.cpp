#include <bits/stdc++.h>
#define ll long long
#define ld long double 
#define inf 0x3f3f3f3f
#define FILE "FOURDIV" 
using namespace std;
//**Variable**//
ll n;
ll ans = 0;
vector<ll> prime;
ll isNotPrime[10001007];
ll squarePrime[10010007];
//**Struct**//

//**Function**//
void sang(ll n){
	isNotPrime[0] = isNotPrime[1] = 1;
 	for(ll i = 2; i<=n; i++){
		if(!isNotPrime[i]){
			if(i*i <= n){
				squarePrime[i*i] = 1;
			}
			prime.push_back(i);
			for(ll j = i*i; j<=n; j+=i){
				isNotPrime[j] = 1;
			}
		}
	}
}
ll find(ll n){
	ll l = 0, r = n, mid, val;
	while(l<r){
		mid = (l+r)/2;
		if(mid*mid*mid <= n){
			val = mid;
			l = mid+1;
		}else{
			r = mid;
		}
	}
	auto it = upper_bound(prime.begin(), prime.end(),val);
	while((*it)*(*it)*(*it)<n){
		it++;
	}
	while((*it)*(*it)*(*it)>n){
		it--;
	}
	return (it - prime.begin() + 1);
}
ll product(ll n){
	ll ans = 0;
	auto cur = prime.begin();
	for(auto it : prime){
		ll add = upper_bound(prime.begin(),cur,(n/it)) - prime.begin() ;
		ans+=add;
		cur++;
	}
	return ans;
}
ll solve(ll n){
	ll ans = 0;
	ans+=find(n);
	ans+=product(n);
	return ans;
}
