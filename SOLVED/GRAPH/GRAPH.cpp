#include <bits/stdc++.h>
#define ll long long
#define ld long double 
#define inf 0x3f3f3f3f
#define FILE "GRAPH" 
using namespace std;
//**Variable**//
int n,m;
int u,v;
vector<int> g[1000006];
ll ans = 0;
//**Struct**//

//**Function**//
ll giaithua(int x){
	if(x <= 0) return 0;
	if(x == 1) return 1;
	return	x*giaithua(x-1);
}
int main()
{
    ios_base::sync_with_stdio(false);cin.tie(NULL);
    // freopen(FILE".inp","w",stdout);
    freopen(FILE".inp","r",stdin);
    freopen(FILE".out","w",stdout);
    cin>>n>>m;
    for(int i = 1; i<=m; i++){
    	cin>>u>>v;
    	v+=n;
    	g[u].push_back(v);
    }
    for(int i = 1; i<=n; i++){
    	sort(g[i].begin(),g[i].end());
    }
    for(int i = 1; i<=n; i++){
    	for(int j = i+1; j<=n; j++){
    		ll cnt = 0;
    		int l = 0, r = 0;
    		while( l<g[i].size() && r<g[j].size() ){
    			if(g[i][l] == g[j][r]){
    				cnt++;
    				l++;r++;
    				continue;
    			}
    			if(g[i][l] < g[j][r]){
    				l++;
    				continue;
    			}
    			if(g[i][l] > g[j][r]){
    				r++;
    				continue;
    			}
    		}
    		ans+= cnt*(cnt-1)/2;
    	}
    }
    cout<<ans;
    return 0;
}
