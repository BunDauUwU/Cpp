//**Struct**//
struct Trie{
	ll cur = 0;
	ll cnt = 0;
	struct node{
		ll val, cnt;
		ll child[29];
	}Node[1000006];
	void build(){
		cur = 0;
		memset(Node[cur].child,-1,sizeof(Node[cur].child));
		Node[cur].val = Node[cur].cnt = 0;
	}
	ll new_node(){
		cur++;
		memset(Node[cur].child,-1,sizeof(Node[cur].child));
		Node[cur].val = 0;
		Node[cur].cnt = 0;
		return cur;
	}
	void add(string s){
		int pos = 0, idx = 0;
		for(int i = 0; i<s.size(); i++){
			idx = s[i] - 'a';
			if(Node[pos].child[idx] == -1){
				Node[pos].child[idx] = new_node();
			}
			pos = Node[pos].child[idx];
			Node[pos].cnt++;
		}
		Node[pos].val++;
	}
	void dfs(int pos){
		if(pos!=0)cnt++;
        for(int i=0;i<26;i++){
            if(Node[pos].child[i]!=-1){
                dfs(Node[pos].child[i]);
                cnt++;
            }
        }

    }
};
//**Variable**//
int n;
string s;
//**Function**//

int main()
{
    ios_base::sync_with_stdio(false);cin.tie(NULL);
    // freopen(FILE".inp","w",stdout);
    // freopen(FILE".inp","r",stdin);
    // freopen(FILE".out","w",stdout);
    cin>>n;
    Trie T;
    T.build();
    ll l = 0;
    ll tmp =0;
    for(int i = 1; i<=n; i++){
    	cin>>s;
    	tmp = s.size();
    	l = max(l,tmp);
    	T.add(s);
    }
    T.dfs(0);
    cout<<T.cnt+n-l;
    return 0;
}
