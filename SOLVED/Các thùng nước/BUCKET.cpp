#define FILE "BUCKET" 
using namespace std;
//**Variable**//
int p;
int t, x, y;
int par[1000006];
int sz[1000006];
//**Struct**//

//**Function**//
void make_set(){
	for(int i = 1; i<=1000005; i++){
		par[i] = i;
		sz[i] = 1;
	}
}
int find(int u){
	if(u == par[u]) return u;
	return par[u] = find(par[u]);
}
bool Union(int u, int v){
	int a = find(u);
	int b = find(v);
	if(a==b)return false		;
	if(sz[a] < sz[b]) swap(a,b);
	sz[a] += sz[b];
	par[b] = a;
	return true;
}
int main()
{
    ios_base::sync_with_stdio(false);cin.tie(NULL);
    // freopen(FILE".inp","w",stdout);
    freopen(FILE".inp","r",stdin);
    freopen(FILE".out","w",stdout);
    cin>>p;
    make_set();
    while(p--){
    	cin>>x>>y>>t;
    	if(t==1){
    		Union(x,y);
    	}else{
    		int u = find(x);
    		int v = find(y);
    		if(u==v){
    			cout<<"1\n";
    		}else{
    			cout<<"0\n";
    		}
     	}
    }
    return 0;
}
