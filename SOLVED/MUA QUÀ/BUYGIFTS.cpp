#define FILE "BUYGIFTS" 
using namespace std;
//**Variable**//
int n, m;
int A[1000006];
//**Struct**//

//**Function**//

int main()
{
    ios_base::sync_with_stdio(false);cin.tie(NULL);
    // freopen(FILE".inp","w",stdout);
    freopen(FILE".inp","r",stdin);
    freopen(FILE".out","w",stdout);
    cin>>n>>m;
    int ans = 1e9 + 7;
    for(int i = 1; i<=n; i++){
    	cin>>A[i];
    }
    sort(A+1,A+1+n);
    for(int i = 1; i<=n-m+1; i++){
    	ans = min(ans, A[i+m-1] - A[i]);
    }
    cout<<ans;
    return 0;
}
