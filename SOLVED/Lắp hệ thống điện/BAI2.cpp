#include <bits/stdc++.h>
#define ll long long
#define ld long double 
#define inf 0x3f3f3f3f
#define FILE "BAI2" 
using namespace std;
//**Variable**//
int n, m;
ld x,y;
int par[1000006];
int sz[1000006];
vector<pair<ld,ld>>v;
vector<pair<pair<int,int>,ld>> A;
//**Struct**//

//**Function**//
void make_set(){
	for(int i = 1;i<1000006; i++){
		sz[i] = 1;
		par[i] = i;
	}
}
ll find(ll u){
	if(par[u] == u)return u;
	return par[u] = find(par[u]);
}
ld calc(pair<ld,ld> v1, pair<ld,ld> v2){
	return sqrt((v2.first - v1.first)*(v2.first - v1.first)+(v2.second - v1.second)*(v2.second - v1.second));
}
bool cmp(pair<pair<int,int>,ld> v1, pair<pair<int,int>,ld> v2){
	return v1.second < v2.second;
}
int main()
{
    ios_base::sync_with_stdio(false);cin.tie(NULL);
    // freopen(FILE".inp","w",stdout);
    freopen(FILE".inp","r",stdin);
    freopen(FILE".out","w",stdout);
    cin>>n>>m;
    for(int i = 0; i<n; i++){
    	cin>>x>>y;
    	v.push_back({x,y});
    }
    make_set();
    int cur = 0;
    for(int i = 0; i<m; i++){
    	cin>>x>>y;
    	x--;
    	y--;
    	int a = find(x);
    	int b = find(y);
    	if(a==b)continue;
    	cur++;
    	if(sz[a]<sz[b])swap(a,b);
    	sz[a] += sz[b];
    	sz[b] = 0;
    	par[b] = a;
    }
    for(int i = 0; i<n; i++){
    	for(int j = i+1; j<n; j++){
    		A.push_back({{i,j},calc(v[i],v[j])});
    	}
    }
    ld ans = 0;
    sort(A.begin(),A.end(),cmp);
    for(auto it : A){
    	if(cur>=n-1)break;
    	int u = it.first.first;
    	int v = it.first.second;
    	int a = find(u);
    	int b = find(v);
    	if(a==b)continue;
    	ans+=it.second;
    	cur++;
    	if(sz[a]<sz[b])swap(a,b);
    	sz[a] += sz[b];
    	sz[b] = 0;
    	par[b] = a;
    }
    cout<<fixed<<setprecision(3)<<ans;
    return 0;
}
