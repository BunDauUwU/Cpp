//**Variable**//
int n;
ll res = -1e18;
ll A[1000006];
//**Function**//
ll max_Sum(int st, int ed){
	if(st==ed)return A[st];
	ll sum = 0;
	ll ans = -1e18;
	ll mn = 1e18;
	for(int i = st; i<=ed ;i++){
		sum+=A[i];
		ans = max(ans , sum-mn);
		mn = min(mn,sum);
	}
	return ans;
}
