int n,q;
int u,v;
vector<int> g[1000006];
vector<int> et(0);
vector<int> l(0);
int first[1000006],h[1000006];
//**Struct**//
struct RMQ{
	vector<vector<int>> table;
	vector<vector<int>> ans;
	vector<vector<int>> res;
	int logn;
	RMQ(vector<int> arr){
		int n = arr.size();
		logn = log2(n);
		ans.resize(n+2,vector<int>(logn+1));
		res.resize(n+2,vector<int>(logn+1));
		table.resize(n+2,vector<int>(logn+1));
		for(int i = 0;i<n;i++){
			table[i][0] = arr[i];
			ans[i][0] = i;
		}
		for(int j = 1;j<=logn;j++){
			for(int i = 0;i+(1<<j)<n;i++){
				if(table[i][j-1]<table[i+(1<<(j-1))][j-1]){
					table[i][j] = table[i][j-1];
					ans[i][j] = ans[i][j-1];
				}else{
					table[i][j] = table[i+(1<<(j-1))][j-1];
					ans[i][j] = ans[i+(1<<(j-1))][j-1];
				}
			}
		}
	}
	int query(int u,int v){
		int l = first[u];
		int r = first[v];
		if(l>r)swap(l,r);
		int n = r-l+1;
		int res = 0;
		int logn = log2(n);
		if(table[l][logn]<=table[r-(1<<logn)+1][logn]){
			res = ans[l][logn];
		}else{
			res = ans[r-(1<<logn)+1][logn];
		}
		return et[res];
	}
	void preprocess(){
		for(int i = 1;i<=n;i++){
			res[i][0] = i;
		}
		for(int j = 1; j<=logn;j++){
			for(int i = 1; i+(1<<j)-1<=n;i++){
				res[i][j] = query(res[i][j-1],res[i+(1<<(j-1))][j-1]);
			}
		}
		// for(int j = 0; j<=logn;j++){
			// for(int i = 1; i+(1<<j)-1<=n;i++){
				// cout<<i<<" "<<i+(1<<j)-1<<":"<<res[i][j]<<"\n";
			// }
		// }
	}
	int lca(int u,int v){
		int ans;
		int n = v-u+1;
		int logN = log2(n); 
		// cout<<u<<" "<<logN<<" "<<v<<":\n";
		if((1<<logN)==n)return res[u][logN];
		ans= query(res[u][logN],res[v-(1<<logN)+1][logN]);
		return ans;
	}
};
//**Function**//
void dfs(int u,int par){
	et.push_back(u);
	if(!first[u]){
		first[u] = et.size()-1;
	}
	for(auto v : g[u]){
		if(v!=par){
			h[v] = h[u]+1;
			dfs(v,u);
			et.push_back(u);
		}
	}
}

