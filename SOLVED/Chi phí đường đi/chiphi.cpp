#include <bits/stdc++.h>
#define ll long long
#define ld long double 
#define inf 0x3f3f3f3f
#define FILE "chiphi" 
#define xiao cout
#define ciin cin
#define tt cout<<"TrangTrangVn❀";
#define f1(a,b) for(int i=a;i<=b;i++)
using namespace std;
//**Struct**//

//**Variable**//
ld S;
ld V,C,P;
ld d[1000006],p[1000006];
int n;
ld dp[1000][1000];
//**Function**//
int calc(ld x){
    int idx = lower_bound(d+1,d+1+n,x) - d;
    if(idx==8) idx--;
    return idx;
}
int main()
{
    ios_base::sync_with_stdio(false);cin.tie(NULL);
    // freopen(FILE".inp","w",stdout);
    freopen(FILE".inp","r",stdin);
    freopen(FILE".out","w",stdout);
    cin>>S>>V>>C>>P>>n;
    for(int i = 1; i<=n; i++){
        cin>>d[i]>>p[i];
        p[i]/=100;
    }
    n++;
    d[n]=S;
    p[n]=0;
    for(int i = 0; i<=n; i++){
        for(int j = 0; j<=n; j++){
            dp[i][j] = inf;
        }
    }
    dp[0][0] = P;
    int idx = calc((V/2)*C);
    for(int i = idx; i<=n; i++){
        ld cur = V - (d[i]/C);
        if(cur<0)break; 
        dp[0][i] = dp[0][0]+(d[i]/C)*p[i]+2;
    }
    for(int i = idx; i<n; i++){
        int idx2 = calc(d[i]+(V/2)*C);
        // cout<<idx2<<" "<<d[i]<(V/2)*C<"+"<<(V/2)*C<<"="<<d[i]+(V/2)*C<<"\n";
        for(int j = idx2; j<=n; j++){
            ld cur = V - ((d[j]-d[i])/C);
            if(cur<0)break;
            for(int k = 0; k<i; k++){
                ld pre = (d[i]-d[k])/C;
                if(pre>V)continue;
                dp[i][j] = min(dp[i][j], dp[k][i] + ((d[j]-d[i])/C)*p[j] + 2);
            }
        }
    }
    idx = calc(S - (V)*C);
    ld ans = inf;
    for(int i = idx; i<=n; i++){
        ans = min(ans,dp[i][n]-2);
    }
    cout<<ans;
    return 0;
}
