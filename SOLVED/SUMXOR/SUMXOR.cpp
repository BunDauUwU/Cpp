#include <bits/stdc++.h>
#define ll long long
#define ld long double 
#define inf 0x3f3f3f3f
#define FILE "SUMXOR" 
using namespace std;
//**Variable**//
int n, m;
int t, l, r, x;
//**Struct**//
struct Seg { 
    vector<int> arr;
    int n;
    int T[4*100006 + 5];
    int lazy[4*100006 + 5];
    Seg(vector<int> a) {
        arr = a;
        n = arr.size();
        build(1, 0, n - 1);
    }
    void build(int id, int l, int r) {
        if (l == r) {
            T[id] = arr[l];
        } else {
            int mid = (l + r) >> 1;
            build(2 * id, l, mid);
            build(2 * id + 1, mid + 1, r);
            T[id] = T[2 * id] + T[2 * id + 1];
        }
    }
    void push(int id, int l, int r) {
        if(l > r) return ;
        if (lazy[id]) {
            T[id] = (r - l + 1) - T[id];
        }
        if (l != r) {
                lazy[2 * id] ^= lazy[id];
                lazy[2 * id + 1] ^= lazy[id];
        }
        lazy[id] = 0;
    }

    void flipRange(int id, int l, int r, int u, int v) {
        if (l > r || r < u || l > v) {
            return;
        }
        push(id, l, r);
        if (l >= u && r <= v) {
            lazy[id] = 1;
            return ;
        }
        int mid = (l + r) >> 1;
        flipRange(2 * id, l, mid, u, v);
        flipRange(2 * id + 1, mid + 1, r, u, v);
        push(2 * id, l, mid);
        push(2 * id + 1, mid + 1, r);
        T[id] = T[2 * id] + T[2 * id + 1];
    }
    int getSum(int id, int l, int r, int u, int v) {
        if (l > r || r < u || l > v) {
            return 0;
        }
        push(id, l, r);
        if (l >= u && r <= v) {
            return T[id];
        }
        int mid = (l + r) / 2;
        return  getSum(2 * id, l, mid, u, v) + getSum(2 * id + 1, mid + 1, r, u, v);
    }

    void flipRange(int l, int r) {
        flipRange(1, 0, n - 1, l, r);
    }
    int getSum(int l, int r) {
        return getSum(1, 0, n - 1, l, r);
    }

};

//**Function**//

int main()
{
    ios_base::sync_with_stdio(false);cin.tie(NULL);
    // freopen(FILE".inp","w",stdout);
    freopen(FILE".inp","r",stdin);
    freopen(FILE".out","w",stdout);
    cin>>n;

    int v[400006];
    for(int i = 0; i < n; i++) {
        cin >> v[i];
    }
    vector<Seg> sg;

    for(int i = 0; i <= 19; i++) {
        vector<int> arr(n, 0);
        for(int j = 0; j < n; j++) {
            if((v[j] & (1 << i)) > 0) {
                arr[j] = 1;
            }
        }
        sg.push_back(Seg(arr));
    }
    cin >> m;
    while(m--) {
        cin >> t;
        if (t == 2) {
            cin >> l >> r >> x;
            l = l - 1;
            r = r - 1;
            for(int i = 0; i <= 19; i++) {
                if((x & (1 << i)) > 0) {
                    sg[i].flipRange(l, r);
                }
            }
        } else {
            cin >> l >> r;
            l = l - 1;
            r = r - 1;
            ll c = 0;
            for(int i = 0; i <= 19; i++) {
                c = c + (sg[i].getSum(l, r) * (1ll << i));
            }
            cout << c << "\n";
        }
    }
    return 0;
}
