#include <bits/stdc++.h>
#define ll long long
#define ld long double 
#define inf 0x3f3f3f3f
#define FILE "ASUMi" 
using namespace std;
//**Variable**//
ll n, k;
ll A[1000006];
ll ans = 0;
//**Struct**//

//**Function**//

int main()
{
    ios_base::sync_with_stdio(false);cin.tie(NULL);
    // freopen(FILE".inp","w",stdout);
    freopen(FILE".inp","r",stdin);
    freopen(FILE".out","w",stdout);
	cin>>n>>k;
	for(int i = 0; i<n; i++) cin>>A[i];
	for(int i = 1; i<n; i++){
		A[i]+=A[i-1];
		if(abs(A[i])>k) ans++;
	}
	if(abs(A[0])>k)ans++;
	sort(A,A+n);
	for(int i = 0; i<n; i++){
		ans +=  n - (upper_bound(A,A+n, A[i] + k)-A);  
	}
	cout<<ans;
    return 0;
}
