#include <bits/stdc++.h>
#define ll long long
#define ld long double 
#define inf 0x3f3f3f3f
#define FILE "LISYS" 
using namespace std;
//**Variable**//
int m, n, s;
int row[1000006], col[1000006];
//**Struct**//

//**Function**//

int main()
{
    ios_base::sync_with_stdio(false);cin.tie(NULL);
    // freopen(FILE".inp","w",stdout);
    freopen(FILE".inp","r",stdin);
    freopen(FILE".out","w",stdout);
    cin>>m>>n>>s;
    int A[m+5][n+5];
	int t;
	for(int i = 1; i<=m; i++){
		for(int j = 1; j<=n; j++){
			cin>>A[i][j];
		}
	}
	for(int i = 1; i<=s; i++){
		cin>>t;
		if(i<=m){
			row[t]++;
		}else{
			col[t]++;
		}
	}
	ll cnt = 0;
	for(int i = 1; i<=m; i++){
		for(int j = 1; j<=n; j++){
			A[i][j]= (A[i][j] + row[i] + col[j])%3;
			if(A[i][j]%3==0){
				cnt++;
			}
		}
	}
	cout << cnt <<"\n";
    return 0;
}