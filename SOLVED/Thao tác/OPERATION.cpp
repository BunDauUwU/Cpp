using namespace std;
//**Variable**//
int n, m, k;
int u, v;
vector<int> g[200006];
int db[21];
int dist[21][200006];
int dp[2000006];
//**Struct**//

//**Function**//
void bfs(int x, int i){
	dist[i][x] = 0;
	queue<int> q;
	q.push(x);
	while(q.size()){
		int u = q.front();q.pop();
		for(auto v : g[u]){
			if(dist[i][v] > dist[i][u] + 1){
				dist[i][v] = dist[i][u] + 1;  
				q.push(v);
			}
		}
	}
}
