#include <bits/stdc++.h>
#define ll long long
#define ld long double 
#define inf 0x3f3f3f3f
#define fi first
#define se second
#define FILE "ISS" 
using namespace std;
//**Variable**//
ll n;
ll N = 100005;
ll A[1000006];
ll W[1000006];
ll BIT[100006];
ll dp[100005];
//**Struct**//

//**Function**//
void update(ll idx, ll val){
	for(;idx<=N;idx += idx & (-idx)){
		BIT[idx] =  max(BIT[idx],val);
	}
}
ll get(ll idx){
	ll ans = 0;
	for(;idx>0;idx -= idx & (-idx)){
		ans = max( ans, BIT[idx]);
	}
	return ans;
}
int main()
{
    ios_base::sync_with_stdio(false);cin.tie(NULL);
    // freopen(FILE".inp","w",stdout);
    freopen(FILE".inp","r",stdin);
    freopen(FILE".out","w",stdout);
    cin>>n;
    for(ll i = 1; i<=n; i++){
    	cin>>A[i];
    }
    for(ll i = 1; i<=n; i++){
    	cin>>W[i];
    }
	update(A[1],W[1]);
	ll ans = 0;
    for(ll i = 1; i<=n; i++){
    	dp[i] = get(A[i]-1) + W[i];
    	update(A[i],dp[i]);
    	ans = max(ans,dp[i]);
    }
    cout<<ans<<"\n";
    return 0;
}
