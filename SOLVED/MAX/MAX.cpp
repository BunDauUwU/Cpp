#include <bits/stdc++.h>
#define ll long long
#define ld long double 
#define inf 0x3f3f3f3f
#define FILE "MAX" 
using namespace std;
//**Variable**//
ll n, k;
ll ans = 0;
ll A[1000006];
stack<pair<ll,ll>> s;
priority_queue<ll> q;
ll l = 0 , r = 0;
//**Struct**//

//**Function**//

int main()
{
    ios_base::sync_with_stdio(false);cin.tie(NULL);
    // freopen(FILE".inp","w",stdout);
    freopen(FILE".inp","r",stdin);
    freopen(FILE".out","w",stdout);
    cin>>n>>k;
    for(int i = 0; i<n; i++){
    	cin>>A[i];
    }
    while(r<n){
    	l = r;
    	while( l < n-1 && A[l] >= A[l+1]) l++;
    	r = l+1;
    	while( r < n && A[r] <= A[r-1]) r++;
    	while( s.size() && A[l] < A[s.top().first] ){
    		auto it = s.top();
    		// cout<<"1."<< A[it.first]<<" "<<A[it.second]<<"\n";
    		q.push( A[it.second ] - A[it.first] );
    		s.pop();
    	}
    	while( s.size() && A[r ] > A[s.top().second ]){
    		auto it = s.top();
    		// cout<<"2."<< A[l]<<" "<<A[it.second]<<"\n";
    		q.push( A[it.second ] - A[l] );
    		l = it.first;
    		s.pop();
    	}
    	s.push({l,r});
    }
    while(s.size()){
    	auto it = s.top();
    	// cout<<"3."<< A[it.first]<<" "<<A[it.second]<<"\n";
    	q.push(A[it.second ] - A[it.first]);
    	s.pop();
    }
    while( k-- &&  q.size()){
    	// cout<<q.top()<<" ";
    	ans+=q.top();
    	q.pop();
    }
    cout<<ans<<"\n";
    return 0;
}
