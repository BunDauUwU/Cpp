//**Variable**//
ll st = (1<<30) - 1;
ll t;
ll n;
ll M[1001][1001];
ll ans[1001];
//**Function**//

int main()
{
    ios_base::sync_with_stdio(false);cin.tie(NULL);
    // freopen(FILE".inp","w",stdout);
    freopen(FILE".inp","r",stdin);
    freopen(FILE".out","w",stdout);
	cin>>t;    
	while(t--){
		cin>>n;
		for(int i = 1; i<=n; ++i){
			ans[i] = st;
		}
		for(int i = 1; i<=n; ++i){
			for(int j = 1; j<=n; ++j){
				cin>>M[i][j];
			}
		}
		for(int i = 1; i<=n; ++i){
			for(int j = 1; j<=n; ++j){
				if(i!=j){
					ans[i]&=M[i][j];
					ans[j]&=M[i][j];
				}
			}
		}
		bool check = 1;
		for(int i = 1; i<=n; ++i){
			for(int j = 1; j<=n; ++j){
				if(i!=j){
					check &= ((ans[i]|ans[j]) == M[i][j]);
				}
				if(!check){
					break;
				}
			}
		}
		if(!check){
			cout<<"NO\n";continue;
		}
		cout<<"YES\n";
		for(int i = 1; i<=n; ++i){
			cout<<ans[i]<<" ";
		}
		cout<<"\n";
	}
    return 0;
}
