#include <bits/stdc++.h>
#define ll long long
#define ld long double 
#define inf 0x3f3f3f3f
#define FILE "LRXOR" 
using namespace std;
//**Variable**//
ll t, n, A[1000006];

//**Struct**//
struct Trie{
	ll cur;
	ll preXor;
	struct node{
		ll cnt, val;
		ll XOR;
		node *child[5];
		node(){
			child[0] = child[1] = 0;
			val = cnt = 0;
			XOR = 0;
		}
	};
	node* root;
	void build(){
		cur = 0;
		root = new node();
	}
	void add(ll x){
		node* pos = root;
		bool idx;
		for(ll i = 31; i>=0; i--){
			// idx = x & (1ll<<i);
			idx = (x>>i) & 1;
			if(pos -> child[idx] == nullptr){
				pos -> child[idx] = new node();
			}
			pos = pos -> child[idx];
			pos -> cnt++;
		}
		pos -> val++;
		pos -> XOR = x;
	}
	ll query(ll x){
		node *pos = root;
		bool idx;
		for(ll i = 31; i>=0; i--){
			// idx = x & (1ll<<i);
			idx = (x>>i) & 1;
			if(pos -> child[ 1 - idx ] != nullptr){
				pos = pos -> child[ 1 - idx ];
			}else if(pos -> child[idx] != nullptr){
				pos = pos -> child[idx];
			}
		}
		return x ^ (pos -> XOR);
	}
};
//**Function**//

int main()
{
    ios_base::sync_with_stdio(false);cin.tie(NULL);
    // freopen(FILE".inp","w",stdout);
    freopen(FILE".inp","r",stdin);
    freopen(FILE".out","w",stdout);
    cin>>t;
    while(t--){
    	cin>>n;
    	Trie T;
    	T.build();
    	ll tmp = 0;
    	ll res = -1e18;
    	for(int i = 0; i<n; i++){
    		cin>>A[i];
    		tmp= tmp ^ A[i];	
    		T.add(tmp);
    		res = max(res, T.query(tmp));
    	}
    	cout<<res<<"\n";
    }
    
    return 0;
}
