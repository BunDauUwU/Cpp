#include <algorithm>
#include <bits/stdc++.h>
#define ll long long
#define ld long double 
#define inf 0x3f3f3f3f
#define FILE "GALAKSIJA" 
using namespace std;
//**Variable**//
ll n;
ll u, v, w;
ll A[1000006];
ll par[1000006];
ll sz[1000006];
ll XOR[1000006];
ll res[1000006];
ll ans = 0;
map<ll, vector<ll>> m[1000006];
//**Struct**//
struct CASE{
	ll u,v,w;
} C[1000006];
//**Function**//
void make_set(){
	for(ll i = 1; i<=n; i++){
		m[i][0].push_back(i);
		par[i] = i;
		sz[i] = 1;
	}
}
void Union(ll u, ll v, ll c){
	ll a = par[u];
	ll b = par[v];
	if(sz[a] < sz[b]){swap(a,b);swap(u,v);}
	for(auto it : m[b]){
		for(auto x : it.second){
			ans += m[a][ XOR[u] ^ XOR[v] ^ XOR[x] ^ c ].size(); 
		}
	}
	ll pre = XOR[v];
	for(auto it : m[b]){
		for(auto x : it.second){
			XOR[x] = XOR[x] ^ XOR[u] ^ pre ^ c ;
			par[x] = a;
			m[a][XOR[x]].push_back(x);
		}
	}
	sz[a] += sz[b];
	sz[b] = 0;
	m[b].clear();
	par[b] = a;
}
int main()
{
    ios_base::sync_with_stdio(false);cin.tie(NULL);
    // freopen(FILE".inp","w",stdout);
    freopen(FILE".inp","r",stdin);
    freopen(FILE".out","w",stdout);
    cin>>n;
    for(ll i = 1; i<=n-1; i++){
    	cin>>C[i].u>>C[i].v>>C[i].w;
		// C[i].u --;
		// C[i].v --;
    }
    for(ll i = 1; i<=n-1; i++){
    	cin>>A[i];
		// A[i]--;
    }
    make_set();
    for(ll i = n-1; i>0; i--){
    	u = C[A[i]].u;
    	v = C[A[i]].v;
    	w = C[A[i]].w;
		Union(u,v,w);
		res[i] = ans;
    }
    for(ll i = 1; i<=n; i++){
    	cout<<res[i]<<"\n";
    }
    return 0;
}
