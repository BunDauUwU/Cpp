#include <bits/stdc++.h>
#define ll long long
#define ld long double 
#define inf 0x3f3f3f3f
#define FILE "INVD" 
using namespace std;
//**Variable**//
ll n,d;
ll A[1000006];
ll BIT[3 * 100005 + 5];
ll l[1000006], r[1000006];
vector<ll> rar;
//**Struct**//

//**Function**//
void update(int idx){
	while(idx <= 3 * 100005){
		BIT[idx]++;
		idx = idx + (idx &(-idx));
	}
}
ll get(int idx){
	ll res = 0;
	while(idx>0){
		res = res + BIT[idx];
		idx = idx - ( idx & -idx );
	}
	return res;
}

int main()
{
    ios_base::sync_with_stdio(false);cin.tie(NULL);
    // freopen(FILE".inp","w",stdout);
    freopen(FILE".inp","r",stdin);
    freopen(FILE".out","w",stdout);
    cin>>n>>d;
    for(int i = 1; i<=n; i++) cin>>A[i];
    for(int i = 1; i<=n; i++){
    	rar.push_back(A[i]);
    	rar.push_back(A[i]-d);
    	rar.push_back(A[i]+d);
    }
    sort(rar.begin(), rar.end());
    rar.resize( unique(rar.begin(),rar.end()) - rar.begin() );
    for(int i = 1; i<=n; i++){
    	int pos = lower_bound(rar.begin(),rar.end(), A[i] + d) - rar.begin();
    	l[i] = get(3 * 100005 + 5) - get(pos - 1);
    	int idx = lower_bound(rar.begin(),rar.end(), A[i]) - rar.begin();
    	update(idx);
    } 
    memset(BIT, 0, sizeof(BIT));
    for(int i = n; i>=1; i--){
    	int pos = lower_bound(rar.begin(),rar.end(), A[i] - d) - rar.begin();
    	r[i] = get(pos);
    	int idx = lower_bound(rar.begin(),rar.end(), A[i]) - rar.begin();
    	update(idx);
    }
    ll ans = 0;
    for(int i = 1; i<=n; i++){
    	ans= ans + l[i]*r[i];
    }
    cout<<ans;
    return 0;
}