#include <bits/stdc++.h>
#define ll long long
#define ld long double 
#define inf 0x3f3f3f3f
#define FILE "BOXES" 
using namespace std;
//**Variable**//
int n, k;
ll mask;
int c[21][21];
int f[(1<<20)];
//**Struct**//

//**Function**//

int main()
{
    ios_base::sync_with_stdio(false);cin.tie(NULL);
    // freopen(FILE".inp","w",stdout);
    freopen(FILE".inp","r",stdin);
    freopen(FILE".out","w",stdout);
    cin>>n>>k;
    for(int i = 1; i<=n; i++){
    	for(int j = 1; j<=n; j++){
    		cin>>c[i][j];
    	}
    }
    memset(f, inf, sizeof(f));
    f[(1<<n)-1] = 0;
    int ans = inf;
   	for(ll mask = (1<<n)-1; mask >=0; mask--){
   		for(int i = 1; i<=n; i++){
   			if( 1 & (mask>>(i-1))){
   				for(int j = 1; j<=n; j++){
   					if(i == j) continue;
   					if( 1 & (mask>>(j-1))){
   						f[mask ^ (1<<(i-1))] = min(f[mask ^ (1<<(i-1))], f[mask] + c[i][j]);
   					}
   				}
   			}
   		}
   	}
   	for(ll mask = (1<<n)-1; mask >=0; mask--){
   		int cnt = 0;
   		for(int i = 1; i<=n; i++){
   			if( 1 & (mask>>(i-1)))cnt++;
   		}
   		if(cnt==k)ans = min(ans, f[mask]);
   	}
   	cout<<ans;
    return 0;
}
