#include <bits/stdc++.h>
#define ll long long
#define ld long double 
#define inf 0x3f3f3f3f
#define fi first
#define se second
#define FILE "SOUV" 
using namespace std;
//**Variable**//
int n, m, k, p;
int u, v, w;
vector<pair<int,int>> adj[100006];
int cost[100006];
int dist[101][10004];
//**Struct**//

//**Function**//
void calc(int x, int i){
	dist[i][x] = 0;
	priority_queue<pair<int,int>,vector<pair<int,int>>,greater<pair<int,int>>> q;
	q.push({dist[i][x],x});
	while(q.size()){
		auto it = q.top(); q.pop();
		int u = it.second;
		for( auto ii : adj[u]){
			int v = ii.se;
			int w = ii.fi;
			if(dist[i][v] > dist[i][u] + w){
				dist[i][v] = dist[i][u] + w;
				q.push({dist[i][v],v});
			}
		}
	}
}
int main()
{
    ios_base::sync_with_stdio(false);cin.tie(NULL);
    // freopen(FILE".inp","w",stdout);
    freopen(FILE".inp","r",stdin);
    freopen(FILE".out","w",stdout);
    cin>>n>>m>>k>>p;
    for(int i = 1; i<=m; i++){
    	cin>>u>>v>>w;
    	adj[u].push_back({w,v});
    	adj[v].push_back({w,u});
    }
    memset(dist, inf, sizeof(dist));
    for(int i = 1; i<=k; i++){
    	cin>>u>>w;
    	calc(u,i);
    	cost[i] = w;
    }
    for(int i = 1; i<=p; i++){
    	int ans = 2*inf; 
    	cin >> u >> v;
    	for(int j = 1; j<=k; j++){
    		ans = min(ans, dist[j][u] + dist[j][v] + cost[j]);
    	}
    	cout<<ans<<"\n";
    }
    return 0;
}
