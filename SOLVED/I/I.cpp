#include <bits/stdc++.h>
#define ll long long
#define ld long double 
#define inf 0x3f3f3f3f
#define FILE "I" 
using namespace std;

//**Variable**//
ll t;
ll k, n;
ll A[1000006];
//**Struct**//

//**Function**//
ll solve(ll mx){
	ll cnt = 0;
	ll cur;
	multiset<int>s;
	for(int i = 1; i<=n; i++)s.insert(A[i]);
	while(s.size()){
		cnt++;
		cur = mx;
		while(s.size()){
			auto it = upper_bound(s.begin(),s.end(), cur);
			if(it == s.begin()) break;
			it--;
			cur -= *it;
			s.erase(it);
		}
	}
	return cnt;
}
int main()
{
    ios_base::sync_with_stdio(false);cin.tie(NULL);
    // freopen(FILE".inp","w",stdout);
    freopen(FILE".inp","r",stdin);
    freopen(FILE".out","w",stdout);
    cin>>t;
    while(t--){
    	ll sum  = 0;
    	ll mx = 0;
    	cin>>n>>k;
    	for(int i = 1; i<=n; i++){
    		cin>>A[i];
    		mx = max(mx,A[i]);
    		sum += A[i];
    	}
    	sort(A+1,A+1+n);
    	ll l = max(mx,sum/k), r = sum, m;
    	while(l < r){
    		m = (l + r) / 2;
    		if(solve(m)>k){
    			l = m + 1;
    		}else{
    			r = m;
    		}
    	}
    	m = (l+r)/2;
    	cout<<m<<"\n";
    }
    return 0;
}
