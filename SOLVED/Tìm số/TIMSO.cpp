//**Function**//
ll cntNumber(ll n){
    ll cnt = 0;
    while (n > 0) {
        n /= 5;
        cnt += n;
    }
    return cnt;
}
 
ll solve(ll n)
{
    ll l = 0;
    ll r = 5e16; 

    while (l < r) {
        ll mid = (l + r) >>1;
        ll count = cntNumber(mid);
        if (count < n)
            l = mid + 1;
        else
            r = mid;
    }
 	return l;
}
