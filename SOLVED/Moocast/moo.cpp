#include <bits/stdc++.h>
#define ll long long
#define ld long double 
#define inf 0x3f3f3f3f
#define FILE "moo" 
using namespace std;
//**Variable**//
int n;
vector<pair<int,int>> v;
vector<pair<pair<int,int>,int>> g;
int x, y;
int par[1000006];
int sz[1000006];
//**Struct**//

//**Function**//
void make_set(){
	for(int i = 0; i<=1000006; i++){
		par[i] = i;
		sz[i] = 1;
	}
}
int find(int u){
	if( u == par[u])return u;
	return par[u] = find(par[u]);
}
bool Union(int u, int v){
	int a = find(u);
	int b = find(v);
	if(a==b)return false;
	if(sz[a]<sz[b]) swap(a,b);
	sz[a]+=sz[b];
	par[b] = a;
	return true;
}
bool cmp(pair<pair<int,int>,int> a, pair<pair<int,int>,int> b){
	return a.second < b.second;
}
ld calc(pair<int,int> a, pair<int,int> b){
	return ((b.first - a.first)*(b.first - a.first) +  (b.second - a.second)*(b.second - a.second));
} 
int main()
{
    ios_base::sync_with_stdio(false);cin.tie(NULL);
    // freopen(FILE".inp","w",stdout);
    freopen(FILE".inp","r",stdin);
    freopen(FILE".out","w",stdout);
	cin>>n;
	for(int i = 1; i<=n; i++){
		cin>>x>>y;
		v.push_back({x,y});
	}    
	for(int i = 0; i<n; i++){
		for(int j = i+1; j<n; j++){
			g.push_back({{i,j},calc(v[i],v[j])});
		}
	}
	sort(g.begin(),g.end(),cmp);
	make_set();
	ll ans = 0;
	int cur = 0;
	for(auto it: g){
		pair<pair<int,int>,int> p = it;
		if(cur>=n-1)break;
		if(Union(p.first.first, p.first.second)){
			cur++;
			ans = max(ans, (ll)p.second);
		}
	}
	cout<<ans;
    return 0;
}
