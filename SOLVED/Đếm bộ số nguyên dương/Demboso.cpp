#include <bits/stdc++.h>
#define ll long long
#define ld long double 
#define inf 0x3f3f3f3f
#define FILE "Demboso" 
using namespace std;
//**Variable**//
int f[55][1000005];
int n, k;
int mod = 1e9+7;
//**Struct**//

//**Function**//

int main()
{
    ios_base::sync_with_stdio(false);cin.tie(NULL);
    // freopen(FILE".inp","w",stdout);
    freopen(FILE".inp","r",stdin);
    freopen(FILE".out","w",stdout);
    cin>>n>>k;
    for(int i = 1; i<=n; i++){
        f[1][i] = 1;
    }
    for(int i = 1; i<=k-1; i++){
        for(int j = 1; j<=n; j++){
            int x = j;
            for( ;x<=n;x+=j){
                f[i+1][x] = (f[i+1][x] + f[i][j])%mod;
            }
        }
    }
    ll ans = 0;
    for(int i = 1; i<=n; i++){
        ans = (ans + f[k][i])%mod; 
    }
    cout<<ans;
    return 0;
}
