#include <bits/stdc++.h>
#define ll long long
#define ld long double 
#define inf 0x3f3f3f3f
#define FILE "TOYS" 
using namespace std;
//**Variable**//
int n;
int A[1000006];
int dp[1000006][4];
//**Struct**//

//**Function**//

int main()
{
    ios_base::sync_with_stdio(false);cin.tie(NULL);
    // freopen(FILE".inp","w",stdout);
    freopen(FILE".inp","r",stdin);
    freopen(FILE".out","w",stdout);
    cin>>n;
    int sum = 0;
    for(int i = 1; i<=n; i++){
    	cin>>A[i];
    }
    memset(dp,0,sizeof(dp));
    dp[1][1] = A[1];
    dp[2][2] = A[1] + A[2];
    dp[3][3] = A[1] + A[2] + A[3];
	for(int i = 1; i<=n; i++){
		for(int x = 1; x<=3; x++){
			if(i<x)break;
			int mx = 0;
			for(int y = 1; y<=3; y++){
				if(i<=x+y)break;
				mx = max(mx, dp[i-x-y][y]);
			}
			if(x==1 && mx) dp[i][x] = mx + A[i];
			if(x==2 && mx) dp[i][x] = mx + A[i] + A[i-1];
			if(x==3 && mx) dp[i][x] = mx + A[i] + A[i-1] + A[i-2];
		}
	}   
	int ans = 0;
	for(int i = 1; i<=n; i++){
		for(int x = 1; x<=3; x++){
			ans = max(ans,dp[i][x]);
		}
	}
	cout<<ans;
    return 0;
}
