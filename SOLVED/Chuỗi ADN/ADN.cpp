//**Function**//
string nen(string s){
    int val[1000006];
    bool m[1000006];
    string f;
    int cur = 0;
    f.push_back(s[0]);
    for(int i = 1; i<s.size(); i++){
        if(s[i] == f[cur]){
            val[cur]++;
        }else{
            f.push_back(s[i]);
            cur++;
        }
    }
    for(int i = 0; i<f.size(); i++){
        val[i]++;
    }
    string res;
    // cout<<f<<"\n";
    if(f[0] == '?'){
        m[0] = 1;
        res.push_back(f[1]);
        len[res.size()-1] = val[0] + val[1];
        val[0] = val[1] = 0;
    }
    for(int i = 0; i<f.size(); i++){
        if(m[i]) continue;
        if(f[i] == '?' && f[i-1] == f[i+1]){
            m[i-1] = m[i] = m[i+1] = 1;
            if(res[res.size()-1] != f[i-1])res.push_back(f[i-1]);
            len[res.size()-1] += val[i] + val[i-1] + val[i+1];
            val[i] = val[i+1] = val[i-1] = 0;
            continue;
        }
        if(f[i] == '?' && i == f.size()-1){
            m[i] = 1;
            len[res.size()-1] += val[i];
            val[i] = 0;
            continue;
        }
        if(res[res.size()-1] != f[i])res.push_back(f[i]);
        len[res.size()-1] += val[i];
        val[i] = 0;
    } 
    // cout<<"\n";
    return res;
}
