#include <bits/stdc++.h>
#define ll long long
#define ld long double 
#define inf 0x3f3f3f3f
#define FILE "SPECIAL" 
using namespace std;
//**Variable**//
int q;
ll n;
vector<int> prime(0); 
//**Struct**//

//**Function**//
void solve(ll n){
	set<ll> ans;
	set<ll> k;
	for(ll i = 1; i<=cbrt(n)+1; i++){
		if(n%i==0){
			k.insert(i);
		}
		if( n%i==0 && n%(i+1)==0){
			ans.insert(i);
		}
	}
	for(auto it : k){
		// if(it == 1)continue;
		ll tmp = sqrt(n/it);
		for(ll i = max(tmp - 10,1ll); i<=tmp+1; i++){
			if(it*i*(i+1) == n)ans.insert(i);
		}
	}
	if(ans.size() == 0){cout<<"-1\n";return;}
	for(auto it: ans){
		cout<<it<<" ";
	}
	cout<<"\n";
}
int main()
{
    ios_base::sync_with_stdio(false);cin.tie(NULL);
    // freopen(FILE".inp","w",stdout);
    freopen(FILE".inp","r",stdin);
    freopen(FILE".out","w",stdout);
    cin>>q;
    while(q--){
    	cin>>n;
    	solve(n);
    }
    
    return 0;
}
