#include <bits/stdc++.h>
#define ll long long
#define ld long double 
#define inf 0x3f3f3f3f
#define fi first
#define se second
#define FILE "EXERCISE" 
using namespace std;
//**Variable**//
ll n, q; 
ll x, d;
ll A[200006];
ll B[200006];
ll up[200006][62];
//**Struct**//

//**Function**//
ll get(ll u, ll k){
	for (ll j = 0; j <= log2(k); ++j)
        if ((k >> j) & 1) u = up[u][j];
    return u;
}
int main()
{
    ios_base::sync_with_stdio(false);cin.tie(NULL);
    // freopen(FILE".inp","w",stdout);
    freopen(FILE".inp","r",stdin);
    freopen(FILE".out","w",stdout);
    cin>>n>>q;
    for(ll i = 1; i<=n; i++){
    	cin>>A[i];
    }
    for(ll i = 1; i<=n; i++){
    	cin>>B[i];
    	up[i][0] = B[i];
    }
    
   	for(ll j = 1; j<62; j++){
   		for(ll i = 1; i<=n; i++){
    		up[i][j] = up[up[i][j-1]][j-1];
    	}
    }
    while(q--){
    	cin>>d>>x;
    	ll ans = get(x,d);
    	cout<<A[ans]<<"\n";
    }
    return 0;
}
