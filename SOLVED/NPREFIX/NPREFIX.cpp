#include <bits/stdc++.h>
#define ll long long
#define ld long double 
#define inf 0x3f3f3f3f
#define FILE "NPREFIX" 
using namespace std;
//**Variable**//
int n, m;
string s;
//**Struct**//
struct Trie{
	struct Node{
		int val, cnt;
		Node* child[26];
		Node(){
			val = cnt = 0;
			for(int i = 0; i<26; i++){
				child[i] = NULL;
			}
		}
	};
	Node* root;
	Trie(){
		root = new Node();
	}
	void add(string s){
		Node* pos = root;
		int idx;
		for(auto it : s){
			idx = it - 'a';
			if(pos -> child[idx] == NULL){
				pos -> child[idx] = new Node();
			}
			pos = pos -> child[idx];
			pos -> cnt++;
		}
		pos -> val++;
	}
	ll check(string s){
		Node* pos = root;
		int idx;
		for(auto it : s){
			idx = it - 'a';
			if(pos -> child[idx] == NULL){
				return 0;
			}
			pos = pos -> child[idx];
		}
		return pos -> cnt;
	}
};
//**Function**//

int main()
{
    ios_base::sync_with_stdio(false);cin.tie(NULL);
    // freopen(FILE".inp","w",stdout);
    freopen(FILE".inp","r",stdin);
    freopen(FILE".out","w",stdout);
    Trie T;
    cin>>n;
    for(int i = 1; i<=n; i++){
    	cin>>s;
    	T.add(s);
    }
    cin>>m;
    for(int i = 1; i<=m; i++){
    	cin>>s;
    	cout<<T.check(s)<<"\n";
    }
    
    return 0;
}
