using namespace std;
//**Struct**//

//**Variable**//
int n,a,b;
int A[2001];
int l,r,mid; 
int dp[3001][3001];
ll ans = -1e18;
//**Function**//
int calc(int w){
	return lower_bound(A+1,A+1+n,w)-A-1;
}
int solve(int w){
	int idx_1,idx_2;
	memset(dp,inf,sizeof(dp));
	dp[0][0]=0;
	for(int i = 1; i<=n; i++){
		idx_1 = calc(A[i]-w+1);
		idx_2 = calc(A[i]-2*w+1);
		for(int j = 0;j <= a; j++){
			if(j>=1)dp[i][j] = min(dp[i][j],dp[idx_1][j-1]); 
			dp[i][j] = min(dp[i][j],dp[idx_2][j]+1);
		}
	}
	for(int i = 0; i<=a; i++){
		if(dp[n][i]<=b){
			return true;
		}
	}
	return false;
}
