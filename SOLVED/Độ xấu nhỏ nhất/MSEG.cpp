#include <bits/stdc++.h>
#define ll long long
#define ld long double 
#define inf 0x3f3f3f3f
#define FILE "MSEQ" 
using namespace std;
//**Variable**//
int n;
ll A[20000006];
ll cnt[20000006];
ll dp[20000006];
//**Struct**//

//**Function**//

int main()
{
    ios_base::sync_with_stdio(false);cin.tie(NULL);
    // freopen(FILE".inp","w",stdout);
    freopen(FILE".inp","r",stdin);
    freopen(FILE".out","w",stdout);
    cin>>n;
    for(int i = 1; i<=n; i++){
    	cin>>A[i];
    }
    dp[0] = 0;
    for(int i = 1; i<=n; i++){
    	if(i-A[i]+1 < 1){
            dp[i] = max(dp[i-1] + 1,dp[i]);
        }else{
            cnt[i - A[i] + 1] ++;
            dp[i] = min(dp[i-1]+1, dp[i-A[i]] + A[i] - cnt[i-A[i]+1]);
        }
    }
    ll ans = dp[n];
    cout<<ans;
    return 0;
}
