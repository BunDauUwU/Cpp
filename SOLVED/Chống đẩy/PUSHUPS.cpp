using namespace std;
//**Struct**//

//**Variable**//
int n,m;
ll A[11];
ll dp[10001][51];
//**Function**//

int main()
{
    ios_base::sync_with_stdio(false);cin.tie(NULL);
    // freopen(FILE".inp","w",stdout);
    freopen(FILE".inp","r",stdin);
    freopen(FILE".out","w",stdout);
    cin>>n>>m;
    for(int i = 1; i<=m; i++){
    	cin>>A[i];
    }
    memset(dp,0,sizeof(dp));
    for(int i = 1; i<=m; i++){
    	dp[A[i]][i] = A[i];
    }
    for(int i = 1; i<=n; i++){
    	for(int j = 1; j<=m; j++){
    		for(int k = 1; k<=m; k++){
    			ll nxt =  dp[i][k] + A[j];    			
    			if(dp[i][k]){
    				dp[i+nxt][j] = max(dp[i+nxt][j],dp[i][k] + A[j]);
    			}
    		}
    	}
    }
    for(int i = 1; i<=n; i++){
    	for(int j = 1; j<=m; j++){
    		cout<<dp[i][j]<<" ";
    	}cout<<"\n";
    }
    ll ans = 0;
    int re = 0;
    for(int i = 1; i<=m; i++){
    	if(ans<dp[n][i]){
    		ans = dp[n][i];
    		re = i;
    	}
    }
    if(ans==0){
    	cout<<-1;
    }else{
    	cout<<ans;
    }
    return 0;
}
