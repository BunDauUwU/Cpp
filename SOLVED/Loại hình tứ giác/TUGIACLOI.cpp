//**Struct**//
struct point{
	ld x,y;
};
struct vect{
	ld x,y;
	ld len;
};
//**Variable**//
point A,B,C,D;
//**Function**//
vect to_vect(point A, point B){
	return {B.x - A.x, B.y - A.y, sqrt((B.x-A.x)*(B.x-A.x)+(B.y-A.y)*(B.y-A.y))};
}
ld dot(vect v1, vect v2){
	return v1.x*v1.y + v2.y*v2.x;
}
ld notDot(vect v1, vect v2){
	return v1.x*v2.y - v1.y*v2.x;
}
int check(){
	vect AB = to_vect(A,B);
	vect BC = to_vect(B,C);
	vect CB = to_vect(C,B);
	vect CD = to_vect(C,D);
	vect AD = to_vect(A,D);
	vect AC = to_vect(A,C);
	vect BD = to_vect(B,D);
	vect DB = to_vect(D,B);
	ld cosABD = dot(AB,BD)/(AB.len*BD.len);
	ld cosBDC = dot(BD,CD)/(BD.len*CD.len);
	ld cosBCA = dot(BC,AC)/(BC.len*AC.len);
	ld cosCAD = dot(AC,AD)/(AC.len*AD.len);
	if(cosABD == cosBDC && cosBCA == cosCAD){
		if(dot(AC,BD)==0 && AC.len == BD.len){
			return 4;
		}
		if(dot(AC,BD)==0){
			//thoi
			return 2;
		}
		if(AC.len==BD.len){
			//chu nhat
			return 3;
		}
		return 1;
	} 
	return 5;
}
