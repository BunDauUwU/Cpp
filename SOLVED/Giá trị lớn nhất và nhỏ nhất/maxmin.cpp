#include <bits/stdc++.h>
#define ll long long
#define ld long double 
#define inf 0x3f3f3f3f
#define FILE "maxmin" 
using namespace std;
//**Variable**//
ll n, x, y;
vector<ll> A(1000006);
//**Struct**//
struct RMQ{
	vector<vector<ll>>mx;
	vector<vector<ll>>mn;
	RMQ(vector<ll> arr){
		ll n = arr.size();
		ll logn = log2(n);
		mx.resize(n,vector<ll>(logn));
		mn.resize(n,vector<ll>(logn));
		for(ll i = 0; i<n; i++){
			mx[i][0] = arr[i];
			mn[i][0] = arr[i];
		}
		for(ll j = 1; j<=logn; j++){
			for(ll i = 0; i + (1<<j) < n; i++){
				mn[i][j] = min(mn[i][j-1], mn[i + (1<<(j-1))][j-1]);
				mx[i][j] = max(mx[i][j-1], mx[i + (1<<(j-1))][j-1]);
			}
		}
	}
	pair<ll,ll> query(ll l, ll r){
		ll n = r - l + 1;
		ll logn = log2(n);
		ll res = min( mn[l][logn], mn[r - (1<<logn) + 1][logn] );
		ll res2 = max( mx[l][logn], mx[r - (1<<logn) + 1][logn] );
		return {res2,res};
	}
};
//**Function**//
int main()
{
    ios_base::sync_with_stdio(false);cin.tie(NULL);
    // freopen(FILE".inp","w",stdout);
    freopen(FILE".inp","r",stdin);
    freopen(FILE".out","w",stdout);
    cin>>n>>x>>y;
    for(ll i = 0; i<n; i++) cin>>A[i];
    RMQ rmq(A);
	ll ans = 0;
	for(ll i = 0; i<n; i++){
		for(ll j = i+1; j<n; j++){
			auto it = rmq.query(i,j);
			ans += ( it.first == max(x,y) & it.second == min(x,y) ); 
		}
	}
    cout<<ans;
    return 0;
}
