using namespace std;
//**Struct**//
struct ti{
	ll h,m;
}A[1000006];
//**Variable**//
int n;
int cnt[1000006];
//**Function**//
bool cmp(ti a, ti b){
	if(a.h==b.h) return a.m < b.m;
	return a.h<b.h;
}
int main()
{
    ios_base::sync_with_stdio(false);cin.tie(NULL);
    // freopen(FILE".inp","w",stdout);
    freopen(FILE".inp","r",stdin);
    freopen(FILE".out","w",stdout);
    cin>>n;
    int ans = 0;
    for(int i = 1; i<=n; i++){
    	cin>>A[i].h>>A[i].m;
    }
    sort(A+1,A+1+n,cmp);
    for(int i = 1; i<=n; i++){
    	cnt[i] = 1;
    }
    for(int i = 2; i<=n; i++){
    	if(A[i].h==A[i-1].h && A[i].m == A[i-1].m){
    		cnt[i] = cnt[i-1]+1;
    	}
    	ans = max(ans,cnt[i]);
    }
    cout<<ans;
    return 0;
}
