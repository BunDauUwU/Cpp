//**Variable**//
map<pair<int,int>,int> m;
int n, A[1000006], ans = 0;
map<int,int>cnt;
//**Function**//

int main()
{
    ios_base::sync_with_stdio(false);cin.tie(NULL);
    // freopen(FILE".inp","w",stdout);
    freopen(FILE".inp","r",stdin);
    freopen(FILE".out","w",stdout);
    cin>>n;
    for(int i = 1; i<=n; i++){
    	cin>>A[i];
    	if(i==1)continue;
    	if(A[i]>=A[i-1]){
    		cnt[A[i]]--;
    		cnt[A[i-1]]++;
    	}else{
    		cnt[A[i-1]]--;
    		cnt[A[i]]++;
    	}
    }
    int sum = 0;
    for(auto it : cnt){
    	sum+=it.second;
    	ans = max(ans,sum);
    }
    cout<<ans;
    return 0;
}
