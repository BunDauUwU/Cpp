#include <bits/stdc++.h>
#define ll long long
#define ld long double 
#define inf 0x3f3f3f3f
#define FILE "Duyenhai" 
using namespace std;
//**Variable**//
int n, k;
int A[100001];
ll dp[100001];
ll mod = 5000000;
ll bit[100001];
//**Struct**//
void update(int idx, int x){
	for(; idx<=100000; idx += idx & (-idx)){
		bit[idx] = (bit[idx] + x)%mod;
	}
}
ll get(int idx){
	ll ans = 0 ;
	for(; idx > 0; idx -= idx & (-idx)){
		ans = (ans + bit[idx])%mod;
	}
	return ans;
}
//**Function**//
void sub3(){
	// memset(dp, 0, sizeof(dp));
	for(int i = 0; i<n; i++)dp[i] = 1;
	for(int l = 2; l<=k; l++){
		memset(bit,0,sizeof(bit));
		for(int i = 0; i<n; i++){
			update(A[i],dp[i]);
			dp[i] = get(A[i]-1);
		}
	}
	ll res = 0;
	for(int i = 0; i<n; i++){
		res = (res + dp[i])%mod;
	}
	cout<<res%mod;
}
