#include <bits/stdc++.h>
#define ll long long
#define ld long double 
#define inf 0x3f3f3f3f
#define FILE "sum" 
using namespace std;
//**Variable**//
ll n, m;
ll A[1001][1001];
ll l[1001][1001], r[1001][1001];
ll t[1001][1001], d[1001][1001];
ll ml[1001][1001], mr[1001][1001];
ll mtop[1001][1001], mdown[1001][1001];
//**Struct**//

//**Function**//

int main()
{
    ios_base::sync_with_stdio(false);cin.tie(NULL);
    // freopen(FILE".inp","w",stdout);
    freopen(FILE".inp","r",stdin);
    freopen(FILE".out","w",stdout);
    cin>>n>>m;
    for(ll i = 1; i<=n; i++){
        for(ll j = 1; j<=m; j++){
            cin>>A[i][j];
        }
    }
    for(ll i = 1; i<=n; i++){
        ll sum=0;
        ll mn=0;
        for(int j = 1; j<=m; j++){
            sum+=A[i][j];
            l[i][j] = sum - mn;
            l[i][j] -= A[i][j];
            mn = min(mn,sum);
        }
        sum = 0;
        mn = 0;
        for(int j = m; j>=1; j--){
            sum+=A[i][j];
            r[i][j] = sum - mn;
            r[i][j] -= A[i][j];
            mn = min(mn,sum);
        }
        
    }
    for(ll j = 1; j<=m; j++){
        ll sum=0;
        ll mn=0;
        for(int i = 1; i<=n; i++){
            sum+=A[i][j];
            t[i][j] = sum - mn;
            t[i][j] -= A[i][j];
            mn = min(mn,sum);
        }   
        sum = 0;
        mn = 0;   
        for(int i = n; i>=1; i--){
            sum+=A[i][j];
            d[i][j] = sum - mn;
            d[i][j] -= A[i][j];
            mn = min(mn,sum);
        }  
    }
    ll ans = -1e18;
    for(ll i = 1; i<=n; i++){
        for(ll j = 1; j<=m; j++){
            ans = max(ans, l[i][j] + r[i][j] + A[i][j]);
            ans = max(ans, l[i][j] + t[i][j] + A[i][j]);
            ans = max(ans, l[i][j] + d[i][j] + A[i][j]);
            ans = max(ans, r[i][j] + t[i][j] + A[i][j]);
            ans = max(ans, r[i][j] + d[i][j] + A[i][j]);
            ans = max(ans, t[i][j] + d[i][j] + A[i][j]);
        }
    }
    cout<<ans;
    return 0;
}