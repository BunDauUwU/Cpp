#include <bits/stdc++.h>
#define ll long long
#define ld long double 
#define inf 0x3f3f3f3f
#define FILE "CNTPAL" 
using namespace std;
//**Variable**//
string s;
int q;
int l, r;
int pal[5002][5002];
int f[5002][5002];
int g[5002][5002];
//**Struct**//

//**Function**//

int main()
{
     ios_base::sync_with_stdio(false);cin.tie(NULL);
    // freopen(FILE".inp","w",stdout);
    freopen(FILE".inp","r",stdin);
    freopen(FILE".out","w",stdout);
    cin>>s>>q;
    memset(pal,0,sizeof(pal));
    memset(f,0,sizeof(f));
    memset(g,0,sizeof(g));
    for(int i = 0; i<s.size(); i++){
    	pal[i][i] = 1;
    }
    for(int gap = 2; gap<=s.size(); gap++){
    	for(int i = 0; i <= s.size() - gap; i++){
    		int j = i + gap - 1;
    		if(i == j-1){
    			pal[i][j] = (s[i] == s[j]);
    		}else{
    			pal[i][j] = (s[i] == s[j]) & pal[i+1][j-1];
    		}
    	}
    }
    for(int i = 0; i<s.size(); i++){
    	int sum = 0;
    	for(int j = i; j<s.size(); j++){
    		sum  += pal[i][j];
    		f[i][j] = sum;
    	}
    }
    for(int j = s.size()-1; j>=0; j--){
    	int sum = 0;
    	for(int i = j; i>=0; i--){
    		sum  += f[i][j];
    		g[i][j] = sum;
    	}
    }
    while(q--){
    	cin>>l>>r;
    	l--;
    	r--; 
    	cout<<g[l][r]<<"\n";
    }
    return 0;
}