#include <bits/stdc++.h>
#define ll long long
#define ld long double 
#define inf 0x3f3f3f3f
#define fi first
#define se second
#define FILE "LCM" 
using namespace std;
//**Variable**//
ll a, b;
ll cnt[10000010];
vector<ll> prime;
set<ll> boiso;
int isnotPrime[10000010];
//**Struct**//

//**Function**//
void sang(){
	isnotPrime[0] = isnotPrime[1] = 1;
	for(ll i = 2; i <= 10000007; i++){
		if(!isnotPrime[i]){
			prime.push_back(i);
			for(ll j = i*i; j<=10000007; j+=i) isnotPrime[j] = 1;
		}
	}
}
void trans(ll  x){
	for(ll i = 0; i<prime.size(); i++){
		while(x % prime[i] == 0 ){
			x/=prime[i];
			cnt[prime[i]]++;
		}
	}
}
int main()
{
    ios_base::sync_with_stdio(false);cin.tie(NULL);
    // freopen(FILE".inp","w",stdout);
    freopen(FILE".inp","r",stdin);
    freopen(FILE".out","w",stdout);
    sang();
    cin>>a>>b;
    for(ll i = a; i<=b; i++){
    	trans(i);
    }
    ll ans = 1;
    for(auto it : prime){
    	if(cnt[it] > 0)
    	ans = (ans *( 2*(cnt[it]) + 1));
    }
    cout<<ans;
    return 0;
}
