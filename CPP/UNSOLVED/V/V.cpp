//**Struct**//
struct Node{
	int par;
	int w,g,b;
	vector<int> child;
	void reset(){
		child.resize(0);
		par= w=g=b = 0;
	}
}nodes[1000006];
//**Variable**//
int t,r;
int n,m;
queue<int> q;
ll dp[1000006];
vector<int> trace[1000006];
bool vis[1000006];
//**Function**//
vector<int> operator+(vector<int> a, vector<int> b){
	for(auto it : b) a.push_back(it);
	return a;
}

bool cmp(int a,int b){
	if(nodes[a].w==nodes[b].w)return nodes[a].g<nodes[b].g;
	return nodes[a].w < nodes[b].w;
}

void solve(){
	while(q.size()){
		int to = q.front();q.pop();
		int tmp = m;
		sort(nodes[to].child.begin(),nodes[to].child.end(),cmp);
		for(auto i:nodes[to].child){
			if(tmp<nodes[i].w)break;
			tmp = tmp - nodes[i].w;
			dp[to]++;
			trace[to].push_back(i);
		}
		if(!vis[nodes[to].par])q.push(nodes[to].par);
		vis[nodes[to].par]=1;
	}	
}
