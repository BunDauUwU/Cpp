int ans = 0;
int A[1000006],B[1000006];
//**Function**//
void merge(int l, int mid, int r){
	int* arr = new int[r-l+1];
	int i = l; 
	int j = mid+1;
	int pos = 0;
	// for(int i=l; i<=r; i++){
		// cout<<A[i]<<" ";
	// }
	while(i<=mid && j<=r){
		if(A[i]<=A[j]){
			arr[pos++] = A[i++];
		}else{
			arr[pos++] = A[j++];
			ans += mid-i+1; 
		}
	}
	while(i<=mid){
		arr[pos++] = A[i++];
	}
	while(j<=r){
		arr[pos++] = A[j++];
	}
	pos = 0;
	for(int i=l; i<=r; i++){
		A[i] = arr[pos++];
		// cout<<arr[pos]<<" ";
	}

	delete [] arr;
}
void cntInversion(int l,int r){
	if(l>=r)return ;
	int mid = (l+r)/2;
	cntInversion(l,mid);
	cntInversion(mid+1,r);
	merge(l,mid,r);
	// cout<<ans<<"\n";
}
