using namespace std;
//**Struct**//

//**Variable**//
ll n,m;
pair<int,int> inp[100005];
ll dp[100005],DP[100005];
ll sumt[100005],sum[100005];
ll cmp,l;
//**Function**//

int main()
{
    ios_base::sync_with_stdio(false);cin.tie(NULL);
    // freopen(FILE".inp","w",stdout);
    freopen(FILE".inp","r",stdin);
    freopen(FILE".out","w",stdout);
    cin>>n>>m;
    for(int i=1;i<=n;i++){
    	cin>>inp[i].t>>inp[i].r;
    }
    sort(inp+1,inp+1+n);
    for(int i=1;i<=n;i++){
    	sumt[i]=sumt[i-1]+inp[i].t*inp[i].r;
    	sum[i]=sum[i-1]+inp[i].r;
    }
    dp[0]=0ll;
    DP[0]=0ll;
    for(int i=1;i<=n;i++){
    	DP[i]=dp[i-1]+m;
    	dp[i]=dp[i-1]+inp[i].t*inp[i].r;
    	for(int j=l;j<=i;j++){
    		cmp = DP[j]+(sumt[i]-sumt[j-1])-(sum[i]-sum[j-1])*inp[j].t;
    		if(dp[i]>=cmp){
    			dp[i]=cmp;
    			l=j;
    		}
    	}
    }
    cout<<dp[n];
    return 0;
}

