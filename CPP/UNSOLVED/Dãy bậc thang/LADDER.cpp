//**Struct**//
struct RMQ{
	vector<vector<int>> table;
	vector<vector<int>> idx;
	RMQ(vector<int> arr){
		int n = arr.size();
		int logn = log2(n);
		table.resize(n,vector<int>(logn));
		idx.resize(n,vector<int>(logn));
		for(int i = 0; i<n; i++){
			table[i][0] = arr[i];
			idx[i][0] = i;
		}
		for(int j = 1; j<=logn; j++){
			for(int i = 0; i+(1<<j)<n; i++){
				if(table[i][j-1]<table[i+(1<<(j-1))][j-1]){
					idx[i][j]= idx[i][j-1];
				}else{
					idx[i][j]= idx[i+(1<<(j-1))][j-1];
				}
			}
		}
	}
	int query(int l, int r){
		int n = r-l+1; int logn = log2(n);
		return table[l][logn]<table[r-(1<<logn)+1][logn]?idx[l][logn]:idx[r-(1<<logn)+1][logn];
	}
};
//**Variable**//
