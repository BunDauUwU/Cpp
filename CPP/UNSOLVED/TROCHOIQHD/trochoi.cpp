//**Function**//
void dfs(int u,int par){
    h[u] = h[par]+1;
    et.push_back(u);
    if(fm[u]==-1)fm[u] = et.size()-1;
    for(auto it : mst[u]){
        int v = it.second;
        if(v!=par){
            dfs(v,u);
            et.push_back(u);
        }
    }
}
bool cmp(edge a, edge b){
    return a.w>b.w;
}
void make_set(){
    for(int i=1;i<=n;i++){
        par[i] = i;
        sz[i] = 1;
    }
}
int find(int u){
    if(par[u]==u)return u;
    return par[u] = find(par[u]);
}
bool Union(int u,int v){
    int a = find(u);
    int b = find(v);
    if(a==b) return 0;
    if(sz[a]<sz[b])swap(a,b);
    par[b]= a;
    sz[a]+=sz[b];
    return 1;
}
void MST(){
    vector<edge> MST;
    make_set();
    sort(adj.begin(),adj.end(),cmp);    
    for(auto it:adj){
        if(Union(it.u,it.v)){
            MST.push_back({it.u,it.v,it.w});
            mst[it.u].push_back({it.w,it.v});
            mst[it.v].push_back({it.w,it.u});
        }
        if(MST.size()==n-1){
            break;
        }
    }
}
