//**Variable**//
int n,q;
int u,v;
vector<int>g[1000006];
vector<int> euler(0);
vector<int> l(0);
int f[1000006];
int h[1000006];
int up[1000006];
int down[1000006];
//**Function**//
void dfs(int u, int par){
	euler.push_back(u);
	if(!f[u])f[u] = euler.size();
	for(auto v : g[u]){
		if(v!=par){
			h[v]=h[u]+1;
			dfs(v,u);
			euler.push_back(u);
		}
	}
}
void make(int u,int par){
	for(auto v:g[u]){
		if(v!=par && h[u]<h[v]){
			make(v,u);
			down[u] = max(down[u],down[v]);
		}
	}
	down[u] = max(h[u],down[u]);
}
void remake(int u,int par){
	int mx=inf;
	for(auto v:g[u]){
		if(v!=par && h[u]>h[v]){
			remake(v,u);
			mx = min(mx,h[v]);
		}
	}
	up[u] = mx;
}
int find_mid(int u,int v, int lca){
	int dist = h[u]+h[v] - 2*h[lca];
	dist/=2;
	if(h[u]==h[v])return lca;
	if(h[u]<h[v])swap(u,v);
	while(dist--){
		for(auto it : g[u]){
			if(h[it]<h[u]){
				u = it;
				break;
			}
		}
	}
	return u;
}
//**Struct**//
struct RMQ{
	vector<vector<int>> table;
	vector<vector<int>> idx;
	int n;
	RMQ(vector<int> arr){
		n = arr.size();
		int logn = log2(n);
		table.resize(n, vector<int>(logn));
		idx.resize(n, vector<int>(logn));
		for(int i = 0; i<n; i++){
			table[i][0] = arr[i];
			idx[i][0] = i;
		}
		for(int j = 1;j<=logn;j++){
			for(int i = 0;i+(1<<j)<n;i++){
				if(table[i][j-1]<table[i+(1<<(j-1))][j-1]){
					table[i][j] = table[i][j-1];
					idx[i][j] = idx[i][j-1];
				}else{
					table[i][j] = table[i+(1<<(j-1))][j-1];
					idx[i][j] = idx[i+(1<<(j-1))][j-1];
				}
			}
		}

	}
	int query(int u,int v){
		int l = f[u];
   		int r = f[v];
   		if(l>r)swap(l,r);
   		int logn = log2(r-l+1);
		return euler[table[l][logn]<=table[r-(1<<logn)+1][logn] ? idx[l][logn]:idx[r-(1<<logn)+1][logn]];
	}
};
