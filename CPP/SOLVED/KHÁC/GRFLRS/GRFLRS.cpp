//**Struct**//
ll MOD = 1e9+7;
struct point{
	int x,y;
};
struct rec{
	point a,b,c,d;
	ll val;
};
int operator+(rec s1, rec s2){
	ll ans = (s2.val)%MOD;
	rec tmp;
	tmp.a = {max(s1.a.x,s2.a.x),max(s1.a.y,s2.a.y)};
	tmp.b = {min(s1.b.x,s2.b.x),min(s1.b.y,s2.b.y)};
	tmp.d = {min(s1.d.x,s2.d.x),min(s1.d.y,s2.d.y)};
	// cout<<tmp.a.x<<" "<<tmp.a.y<<" ";
	ll h,w;
    h = abs(tmp.a.x - tmp.b.x)+1;
   	w = abs(tmp.a.y - tmp.d.y)+1;
   	ll sum = (h*(h+1)%MOD)*(w*(w+1)%MOD)/4;
   	ans -= sum;
	return ans;
}
//**Variable**//
int n,m,q,k;
map<int,int> wg,fg;
set<int> l,r;
pair<int,int> w[300005];
pair<int,int> fz[300005];
vector<rec>shape;
//**Function**//

int main()
{
    ios_base::sync_with_stdio(false);cin.tie(NULL);
    // freopen(FILE".inp","w",stdout);
    freopen(FILE".inp","r",stdin);
    freopen(FILE".out","w",stdout);
    cin>>n>>m>>q>>k;
    for(int i=1;i<=q;i++){
    	cin>>w[i].first>>w[i].second;
    	if(!wg[w[i].first])wg[w[i].first]=1e9;
    	wg[w[i].first] = min(wg[w[i].first],w[i].second);
    	l.insert(w[i].first);
    }
    for(int i=1;i<=k;i++){
    	cin>>fz[i].first>>fz[i].second;
    	fg[fz[i].first] = max(fg[fz[i].first],fz[i].second);
    	r.insert(fz[i].first);
    }
    point a,b,c,d;
    for(auto i:l){
    	for(auto j:r){
    		a = {i,wg[i]};
    		b = {j,wg[i]};
    		c = {j,fg[j]};
    		d = {i,fg[j]};
    		ll h,w;
    		h = abs(a.x - b.x)+1;
    		w = abs(a.y - d.y)+1;
    		// cout<< h <<" "<<w<<"=";
    		ll tmp = (h*(h+1)%MOD)*(w*(w+1)%MOD)/4;
    		// cout<<tmp<<"\n";
    		shape.push_back({a,b,c,d,tmp});
    	}
    }
    ll res = 0;
    for(int i = 0 ; i < shape.size(); i++ ){
    	// cout<<shape[i].val<<" ";
        res+=shape[i].val;
    	for(int j = i+1;j<shape.size(); j++){
    		res+= (shape[i]+shape[j])%MOD;
    	}
    }
    if(shape.size()==1){
    	res = (n*(n+1))%MOD*(m*(m+1)%MOD)/4;
    }
    cout<<res;
    return 0;
}
