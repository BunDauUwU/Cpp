using namespace std;
//**Struct**//

//**Variable**//
int n,m,q;
ll A[1000006];
set<ll> tmpA;
ll B[1000006];
ll C[1000006];
set<ll> tmpC;
ll mark[1000006];
ll sz = 0;
ll ans = -1e18;
//**Function**//

int main()
{
    ios_base::sync_with_stdio(false);cin.tie(NULL);
    // freopen(FILE".inp","w",stdout);
    freopen(FILE".inp","r",stdin);
    freopen(FILE".out","w",stdout);
    cin>>n>>m>>q;
    for(int i =1;i<=n;i++){
    	cin>>A[i];
    	mark[A[i]]=1;
    	tmpA.insert(A[i]);
    }
    for(int i=1;i<=m;i++){
    	cin>>B[i];
    	mark[B[i]]=-1;
    }
    for(int i =1;i<=q;i++){
    	cin>>C[i];
    }
    sz = 0;
    for(int i =1;i<=q;i++){
    	if(mark[C[i]]==1){
    		tmpC.insert(C[i]);
    		sz++;
    	}
    	if(mark[C[i]]==0){
    		sz++;
    	}
    	if(mark[C[i]]==-1){
    		if(tmpC.size()==tmpA.size()){
    			ans = max(ans,sz);
    		}
    		tmpC.clear();
    		sz = 0;
    	}
    }
    ans = max(ans,sz);
    cout<<ans;
    return 0;
}
