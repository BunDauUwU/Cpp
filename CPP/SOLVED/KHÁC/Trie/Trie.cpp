//**Struct**//
struct Trie{
    struct Nodes{
        int cnt,val; 
        int child[28];
    }node[2000006];
    int cur ;
    void query(){
        cur = 0;
        memset(node[0].child,-1,sizeof(node[0].child));
        node[0].cnt = node[0].val = 0;
    }
    ll newnode(){
        cur++;
        memset(node[cur].child,-1,sizeof(node[cur].child));
        node[cur].cnt = node[cur].val = 0;
        return cur;
    }
    void add(string s){
        int pos = 0;
        int idx = 0;
        for(int i=0;i<s.size();i++){
            idx = s[i] - 'a';
            if(node[pos].child[idx]==-1){
                node[pos].child[idx]=newnode();
                // node[pos].cnt++;
                // continue;
            }
            pos = node[pos].child[idx];
            node[pos].cnt++;
        }
        node[pos].val++;
    }
    void erase(string s){
        int pos = 0;
        int idx = 0;
        for(int i=0;i<s.size();i++){
            idx = s[i] - 'a';
            node[pos].child[idx]=-1;
            pos = node[pos].child[idx];
            node[pos].cnt--;
        }
        node[pos].val--;
    }
    void dfs(int pos,string str, vector<string> ans){
        for(int i=1;i<=node[pos].val;i++){
            ans.push_back(str);
        }
        for(int i=0;i<26;i++){
            if(node[pos].child[i]!=-1){
                str.push_back(char(i+'a'));
                dfs(node[pos].child[i],str,ans);
                str.pop_back();
            }
        }
    }
    void print_str(){
        vector<string> ans(0);
        string s;
        dfs(0,s,ans);
        for(auto it: ans){
            cout<<it<<"\n";
        }
    }
}T;
