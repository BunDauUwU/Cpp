//**Variable**//
int n,m,s,f,u,v;
int tt[1000006];
bool vis[1000006];
vector<int> g[1000006];
//**Function**//
int bfs(int x){
	int cost[1000006];
	// if(tt[x]==0)cost[x]++;
	queue<int> q;
	vis[x] = 1;
	q.push(x);
	while(!q.empty()){
		int u = q.front();
		q.pop();
		for(auto v: g[u]){
			if(!vis[v]){
				cost[v]+=cost[u];
				if(tt[v]==0){
					cost[v]++;
				}
				// cout<<v<<":"<<cost[v]<<" ";
				if(v==f){
					return cost[v];
				}
				vis[v] = 1;
				q.push(v);
			}
		}
		
	}
	return cost[v];
}	
