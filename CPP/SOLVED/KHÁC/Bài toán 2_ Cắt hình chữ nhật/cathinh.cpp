#define f1(a,b) for(int i=a;i<=b;i++)
using namespace std;
//**Struct**//

//**Variable**//
int n,m;
int dp[101][101];
vector<int> trace[101][101];

//**Function**//

int main()
{
    ios_base::sync_with_stdio(false);cin.tie(NULL);
    // freopen(FILE".inp","w",stdout);
    freopen(FILE".inp","r",stdin);
    freopen(FILE".out","w",stdout);
    cin>>n>>m;
    memset(dp,inf,sizeof(dp));
    for(int i= 1;i<=n;i++){
    	for(int j = 1;j<=m;j++){
    		int reI,reJ,reX,reY;
    		if(i==j){
    			trace[i][j].push_back(i);
    			dp[i][j] = 1;
    		}
    		int mn = inf;
    		for(int k = 1;k<=i;k++){
    			if(mn>dp[i-k][j]+dp[k][j]){
    				mn = dp[i-k][j]+dp[k][j];
    				reI = i-k;
    				reJ = j;
    				reX = k;
    				reY = j;
    			}
    		}
    		for(int k = 1;k<=j;k++){
    			if(mn>dp[i][j-k]+dp[i][k]){
					mn = dp[i][j-k]+dp[i][k];	
					reI = i;
					reJ = j-k;
					reX = i;
					reY = k;
    			}
    		}
    		if(mn<dp[i][j]){
    			dp[i][j] = mn;
    			for(auto it: trace[reI][reJ]){
    				trace[i][j].push_back(it);
    			}
    			for(auto it: trace[reX][reY]){
    				trace[i][j].push_back(it);
    			}
    		}
    	}
    }
    cout<<dp[n][m]<<"\n";
    sort(trace[n][m].begin(),trace[n][m].end(),greater<int>());
    for(auto it: trace[n][m]){
    	cout<<it<<" ";
    }
    return 0;
}
