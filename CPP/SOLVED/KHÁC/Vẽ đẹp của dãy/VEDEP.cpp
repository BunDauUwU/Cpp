//**Struct**//

//**Variable**//
int n;
ll A[1000006];
ll exist[1000006];
ll cnt[1000006];
//**Function**//
ll d(ll n){
    cnt[n]++;
    ll ans = cnt[n];
    for(int i = 2;i<=sqrt(n);i++){
        if(exist[i] && n%i==0){
            cnt[i]++;
            ans = max(ans,cnt[i]);
        }
        if(exist[n/i] && n%i==0 && n/i!=i){
                cnt[n/i]++;
                ans = max(ans,cnt[n/i]);
        }
    }
    return ans;
}
