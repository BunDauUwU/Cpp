//**Function**//
int solve(int N, int K)
{	int dp[K]; 
    memset(dp, 0, sizeof(dp)); 
    for (int i = 0; i < N; i++){
        dp[A[i] % K]++; 
    }
    if (K % 2 == 0) dp[K/2] = min(dp[K/2], 1); 
    int res = min(dp[0], 1); 
    for (int i = 1; i <= K/2; i++){
        res += max(dp[i], dp[K-i]); 
    }
    return res; 
}
