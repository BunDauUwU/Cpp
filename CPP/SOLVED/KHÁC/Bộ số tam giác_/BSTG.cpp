using namespace std;
//**Struct**//

//**Variable**//
int n,ans = 0,l,r;
int cnt=0;
int A[100005];
//**Function**//
int calc(int x){
	return lower_bound(A+1,A+1+n,x)-A;
}
int main()
{
    ios_base::sync_with_stdio(false);cin.tie(NULL);
    // freopen(FILE".inp","w",stdout);
    freopen(FILE".inp","r",stdin);
    freopen(FILE".out","w",stdout);
    cin>>n;
    for(int i=1;i<=n;i++){
    	cin>>A[i];
    }
    sort(A+1,A+1+n);
    for(int i=1;i<n-1;i++){
    	for(int j=i+1;j<n;j++){
            cnt = 0;
    		l = calc(A[j]-A[i]+1);
    		r = calc(A[j]+A[i]-1);
            r=min(r,n);
    		cnt+= (r-l);
    		if(A[j]-A[i]+1<=A[i] && A[i]<=A[j]+A[i]-1){
    			cnt--;
    		}
    		if(A[j]-A[i]+1<=A[j] && A[j]<=A[j]+A[i]-1){
    			cnt--;
    		}
            cnt = max(cnt,0);
            ans+=cnt;
            // cout<<A[j]-A[i]+1<<" "<<A[i]+A[j]-1<<"|"<<A[i]<<" "<<A[j]<<"="<<ans<<"\n";
    	}
    }
    cout<<ans;
    return 0;
}
