//**Struct**//

//**Variable**//
int n,K;
ll A[1001];
ll dp[1001][1001];
ll sum[2001];
//**Function**//
int tknp(int l,int r,int val){
    int mid = (l+r)/2;
    while(l<r){
        if(A[mid]<val){
            l = mid+1;
        }
        else{
            r = mid-1;
        }
        mid = (l+r)/2;
    }
    while(A[l]>=val){
        l--;
    }
    return l;
}
int main()
{
    ios_base::sync_with_stdio(false);cin.tie(NULL);
    // freopen(FILE".inp","w",stdout);
    freopen(FILE".inp","r",stdin);
    freopen(FILE".out","w",stdout);
    A[0] = -2000;
    A[n+1] = 2000;
    sum[0] = 0; 
    cin>>n>>K;
    for(int i = 1; i<=n ;i++){
        cin>>A[i];
        sum[i]+=sum[i-1]+A[i];
        dp[i][0] = sum[i] - A[1]*(i); 
    }
    dp[0][0] = 0;
    // dp[n+1][K+1] = A[n+1]-A[n];
    // dp[n+1][K] = A[n+1]-A[n];
    ll sumleft,sumright;
    ll tmp = 0;
    for(int i = 1;i<=n;i++){
        for(int j = 1;j<=min(i,K);j++){
            dp[i][j]=1e18;
            for(int k=j;k<i;k++){
                int l = k, r = i;
                int m = tknp(l,r,(A[l]+A[r])/2);
                sumleft = (sum[m] - sum[l])-A[l]*(m-l);
                sumright = A[r]*(r-(m+1)) - (sum[r-1] - sum[m]);
                tmp = sumleft + sumright;
                m++;
                sumleft = (sum[m] - sum[l])-A[l]*(m-l);
                sumright = A[r]*(r-(m+1)) - (sum[r-1] - sum[m]);
                tmp = min(tmp,sumleft+sumright);
                if((r-l+1)==2)tmp = 0ll;
                dp[i][j] = min(dp[k][j-1]+tmp,dp[i][j]);
            }
        }
    }
    ll ans = 1e18;
    for(int i = 1;i<=n;i++){
        dp[i][K-1]+=dp[i][K-1]+(sum[n]-sum[i])-A[i]*(n-i);
        ans = min(ans,dp[i][K-1]);
    }
    cout<<ans;
    return 0;
}
