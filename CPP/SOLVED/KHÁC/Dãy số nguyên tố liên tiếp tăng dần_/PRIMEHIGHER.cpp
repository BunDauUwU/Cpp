using namespace std;
//**Struct**//

//**Variable**//
int n;
int A[1000006];
int nt[1000003];
int uwu[1000006];
int mx = 0;
//**Function**//
void sang(){
	memset(nt,inf,sizeof(nt));
	nt[0]=nt[1]=0;
	for(int i=2;i<=1000003;i++){
		if(nt[i]){
			for(int j=i*2;j<=1000003;j+=i){
				nt[j] = 0;
			}
		}
	}
}
bool checkPrime(int a){
	if(a==0 && a==1)return 0;
	int i=2;
	while(i*i<=a){
		if(a%i==0)return 0;
		i++;
	}
	return 1;
}
int main()
{
    ios_base::sync_with_stdio(false);cin.tie(NULL);
    // freopen(FILE".inp","w",stdout);
    freopen(FILE".inp","r",stdin);
    freopen(FILE".out","w",stdout);
    sang();
    cin>>n;
    for(int i=1;i<=n;i++){
    	cin>>A[i];
    }
    for(int i=1;i<=n;i++){
    	if(A[i]<=1000003){
    		if(nt[A[i]])uwu[i] = 1;
    	}else{
    		if(checkPrime(A[i])) uwu[i]=1;
    	}
    }
    vector<int> tmp(0),ans;
    int sz = 0;
    for(int i=1;i<=n;i++){
    	while(uwu[i] && A[i]>A[i-1]){
    		tmp.push_back(A[i]);
    		i++;
    	}
    	sz = tmp.size();
    	if(sz>mx){
    		ans.assign(tmp.begin(),tmp.end());
    		mx = max(mx,sz);
    	}
    	tmp.clear();
    }
    cout<<mx<<"\n";
    for(auto it:ans){
    	cout<<it<<" ";
    }
    return 0;
}
