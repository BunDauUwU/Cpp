using namespace std;
//**Struct**//

//**Variable**//
ll t,n,k;
ll A[1000006];
ll dp[1000006];
ll vis[1000006];
//**Function**//
bool solve(ll x,ll k){
	if(A[n]>x)return 0;
	sort(A+1,A+1+n);
	memset(dp,0,sizeof(dp));
	memset(vis,0,sizeof(vis));
	ll tmp,idx;
	ll cnt = 0;
	for(ll i = n; i>0; i--){
		if(!vis[i]){
			vis[i] = 1;
			tmp = x-A[i];
			for(ll j=1;j<=i && tmp>=A[j]; j++){
				if(!vis[j]){
					vis[j] = 1;
					tmp-= A[j];
				}	
			}
			cnt++;
		}
	}
	if(cnt>k)return 0;
	return 1;
}
int main()
{
    ios_base::sync_with_stdio(false);cin.tie(NULL);
    // freopen(FILE".inp","w",stdout);
    freopen(FILE".inp","r",stdin);
    freopen(FILE".out","w",stdout);
    cin>>t;
    while(t--){
    	cin>>n>>k;
    	ll mx=-1e18;
    	for(int i = 1;i<=n;i++){
    		cin>>A[i];
    		mx = max(A[i],mx);
    	}
    	ll l=0, r= 2*mx*n, mid;
    	while(l<r){
    		mid = (l+r)/2;
    		if(solve(mid,k)){
    			r = mid-1;
    		}else{
    			l = mid+1;
    		}
    		// cout<<l<<" "<<r<<"="<<mid<<"\n";
    	}
    	mid = (l+r)/2;
    	while(!solve(mid,k))mid++;
    	cout<<mid<<"\n";
    }
    
    return 0;
}
