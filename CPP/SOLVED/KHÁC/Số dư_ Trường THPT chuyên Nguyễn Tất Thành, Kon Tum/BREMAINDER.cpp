ll solve(int n, int m){
	ll x = 1%m;
	ll y = 2%m;
	for(int i = 1;i<=n;i++){
		x = ((x+1)*(y-1))%m;
		y = (y*y)%m;
	}
	if(x<0)x+=m;
	return x;
}
