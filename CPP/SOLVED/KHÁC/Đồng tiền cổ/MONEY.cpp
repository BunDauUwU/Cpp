//**Struct**//

//**Variable**//
ll n,m,k;
ll s,t;
ll a,b;
ll val[100005];
ll d[100005][2];
ll u,v,w;
vector<pair<ll,ll>> g[100005];
//**Function**//
ll dijiktra(ll x){
	memset(d,inf,sizeof(d));
	priority_queue<pair<ll,ll>,vector<pair<ll,ll>>,greater<pair<ll,ll>>>pq;
	pq.push({0,x});
	d[x][0] = 0;
	d[x][1] = val[x];
	while(pq.size()){
		auto t = pq.top();
		pq.pop();
		ll u = t.second;
		ll kc = t.first;
		if(kc>d[u][0]) continue;
		for(auto it: g[u]){
			int v = it.second;
			int w = it.first;
			if(d[v][0]>d[u][0]+w){
				d[v][0] = d[u][0]+w;
				pq.push({d[v][0],v});
			}
			if(d[v][1]>d[u][0]+w+val[v]){
					d[v][1] = d[u][0]+w+val[v];
				}
			if(d[v][1]>d[u][1]+w){
				d[v][1] = d[u][1]+w;
			}

		}
	}
	return d[t][1];
}
