using namespace std;
//**Struct**//

//**Variable**//
int n,m;
int c[1001][1001], d[1001][1001];
int dp[1001][1001];
//**Function**//

int main()
{
    ios_base::sync_with_stdio(false);cin.tie(NULL);
    // freopen(FILE".inp","w",stdout);
    freopen(FILE".inp","r",stdin);
    freopen(FILE".out","w",stdout);
    cin>>n>>m;
    for(int i = 1; i<=n; i++){
    	for(int j = 1; j<=n; j++){
    		cin>>c[i][j];
    	}
    }
    for(int i = 1; i<=n; i++){
    	for(int j = 1; j<=n; j++){
    		cin>>d[i][j];
    	}
    }
    memset(dp,inf,sizeof(dp));
    for(int j = 1; j<=n; j++){
    	dp[0][j] = 0; 
    }
    for(int i = 1; i<=n; i++){
    	for(int j = 1; j<=n; j++){
    		for(int k = 1; k<=n; k++){
    			if(abs(i-j)<=1){
    				dp[i][j] = min(dp[i][j],dp[i-1][k]+c[i][j]+d[i][j]);
    				continue;
    			}
    			dp[i][j] = min(dp[i][j],dp[i-1][k]+c[i][j]);
    		}
    	}
    }
    int ans = inf;
    for(int j = 1; j<=n; j++){
    	ans = min(ans,dp[n][j]);
    }
    cout<<ans;
    return 0;
}
