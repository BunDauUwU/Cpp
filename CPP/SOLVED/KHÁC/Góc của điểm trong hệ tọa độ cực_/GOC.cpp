//**Struct**//
struct point{
    ld x, y;    
}A,B,C;
struct vect{
    ld x, y;
};
//**Variable**//

//**Function**//
vect to_vect(point A, point B){
    return {B.x-A.x, B.y-A.y};
}
ld len(vect v){
    ld tmp = 1.000000000;
    return sqrt(v.x*v.x+v.y*v.y)*tmp;
}
ld Dot(vect v1 ,vect v2){
    return v1.x*v2.x + v1.y*v2.y;
}
