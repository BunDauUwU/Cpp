using namespace std;
//**Struct**//

//**Variable**//
int t;
int m,n;
int fmask;
int ans = 0;
int f[40][40];
//**Function**//
void preprocess(){
	ans =0;
	for(int i=0;i<=40;i++){
		for(int j=0;j<=40;j++){
			f[i][j] = 0;
		}
	}
	fmask = (1<<n)-1;
}

bool check(int mask1,int mask2){
	for(int i=1;i<n;i++){
		int a = (mask1 & (1<<i))!=0, b = (mask1 & (1<<(i-1)))!=0;
        int c = (mask2 & (1<<i))!=0, d = (mask2 & (1<<(i-1)))!=0;
        if(a==b&&b==c&&c==d) return 0;
	}
	return 1;
}
void solve(){
	preprocess();

	for(int i=0;i<=(1<<n);i++){
		f[1][i] = 1;
	}

	for(int i=2;i<=m;i++){
		for(int td = fmask; td>=0 ; td--){
			for(int ht = fmask; ht>=0; ht--){
				if(check(td,ht))f[i][td]+=f[i-1][ht];
			}
		}
	}
	for(int i= fmask; i>=0;i--){
		ans+=f[m][i];
	}
	cout<<ans<<"\n";
}
int main()
{
    ios_base::sync_with_stdio(false);cin.tie(NULL);
    // freopen(FILE".inp","w",stdout);
    freopen(FILE".inp","r",stdin);
    freopen(FILE".out","w",stdout);
    cin>>t;
    while(t--){
    	cin>>m>>n;
    	if(n>m)swap(m,n);
    	if(n==1){
    		cout<<(1<<m)<<"\n";continue;
    	}
    	solve();
    }
    
    return 0;
}
