int n,m;
int f[101][101];
//**Function**//
int cut(int a, int b){
	if(f[a][b]!=-1){
		return f[a][b];
	}
	if(a==b){
		return f[a][b] = 1;
	}
	f[a][b] = inf;
	for(int i = 1;i<a;i++){
		f[a][b] = min(f[a][b],cut(a-i,b)+cut(i,b));
	}
	for(int i = 1;i<b;i++){
		f[a][b] = min(f[a][b],cut(a,b-i)+cut(a,i));
	}
	// f[a][b]++;
	f[b][a] = f[a][b];
	return f[a][b];
}
