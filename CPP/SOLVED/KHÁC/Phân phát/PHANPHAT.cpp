using namespace std;
//**Struct**//

//**Variable**//
ll n;
vector<int> g[1000006];
ll sz[1000006];
vector<ll> hmmm;
int u,v;
int m;
ll p[1000006];
ll MOD = 1000000007;
//**Function**//
void dfs(int u, int pa){
    sz[u] = 1;
    for(int v : g[u]){
        if(v == pa) continue;
        dfs(v, u);
        sz[u] += sz[v];
        hmmm.push_back(sz[v] * (n - sz[v]));
    }
}
int main()
{
    ios_base::sync_with_stdio(false);cin.tie(NULL);
    // freopen(FILE".inp","w",stdout);
    freopen(FILE".inp","r",stdin);
    freopen(FILE".out","w",stdout);
    cin >> n;
    for (int i = 1; i < n; ++i){
        int u, v; cin >> u >> v;
        g[u].push_back(v);
        g[v].push_back(u);
    }

    dfs(1, 0);
    cin >> m;
    ll ans = 0;
    for(int i = 1; i <= m; ++i) cin >> p[i];
    if (n > m){
        hmmm.push_back(1e18);
        sort(p + 1, p + m + 1, greater<ll>());
        sort(hmmm.begin(), hmmm.end(), greater<ll>());
        for (int i = 1; i <= m; i++){
            ans += p[i] * hmmm[i];
            ans %= MOD;
        }
        for (int i = m + 1; i <= n; i++){
            ans += hmmm[i];
            ans %= MOD;
        }
    }
    else{
        hmmm.push_back(-1e18);
        sort(p + 1, p + m + 1);
        sort(hmmm.begin(), hmmm.end());
        for (int i = 1; i < n - 1; ++i){    
            ans += hmmm[i] * p[i];
            ans %= MOD;
        }
        for (int i = n; i <= m; ++i){
            p[n - 1] *= p[i];
            p[n - 1] %= MOD;
        }
        ans += hmmm[n - 1] * p[n - 1] % MOD;
    }
    cout << ans % MOD << '\n';
    return 0;
}