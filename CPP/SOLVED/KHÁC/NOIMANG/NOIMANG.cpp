using namespace std;
//**Struct**//
struct graph{
	ll u,v,w;
};
//**Variable**//
int n,k;
ll u,v,w;
ll par[1000006],sz[1000006];
vector<graph> g;
vector<graph> inp;
vector<graph> MST;
//**Function**//
bool cmp(graph a,graph b){
	return a.w<b.w;
}
void make(){
	for(int i = 1;i<=n;i++){
		par[i] = i;
		sz[i] = 1;
	}
}
ll find(ll u){
	if(u==par[u])return u;
	return par[u] = find(par[u]);
}
bool Union(ll u,ll v){
	ll a = find(u);
	ll b = find(v);
	if(a==b)return false;
	if(sz[a]<sz[b])swap(a,b);
	par[b] = a;
	return true;
}
int main()
{
    ios_base::sync_with_stdio(false);cin.tie(NULL);
    // freopen(FILE".inp","w",stdout);
    freopen(FILE".inp","r",stdin);
    freopen(FILE".out","w",stdout);
    cin>>n>>k;
    make();
    for(int i=1;i<=k;i++){
    	cin>>u>>v>>w;
    	g.push_back({u,v,w});
    }
    sort(g.begin(),g.end(),cmp);
    for(auto it : g){
    	if(Union(it.u,it.v)){
    		MST.push_back(it);
    	}
    	if(MST.size()==n-1)break;
    }
    if(MST.size()==n-1){cout<<0;return 0;}
    while(cin>>u>>v>>w){
    	inp.push_back({u,v,w});
    }
    sort(inp.begin(),inp.end(),cmp);
    ll cost = 0;
    for(auto it : inp){
    	if(Union(it.u,it.v)){
    		MST.push_back(it);
    		cost+=it.w;
    	}
    	if(MST.size()==n-1)break;
    }
    cout<<cost;
    return 0;
}
