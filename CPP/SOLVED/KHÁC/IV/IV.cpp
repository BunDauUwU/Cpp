void sub1(){
    ll B=0,R=0;
    priority_queue<ll> q;
    for(int i = 1; i<=m; i++){
        q.push(s[i]);        
    }
    for(int i = 1; i<=m; i++){
        if(wic[i]==1)B+=q.top();
        else if(wic[i]==2)R+=q.top();
        q.pop();
    }
    cout<<B-R<<"\n";
}
void sub2(){
    ll B=0,R=0;
    int turnB = m/2/2;
    int ban = 0, pick = 0;
    for(int i = 1; i<=m/2 ;i++){
        if(c[i]=='b')ban++;
        if(c[i]=='p')pick++;
    }
    for(int i = 1; i<=pick; i++){
        B+=s[i];
    }
    ll cur = pick+ban+1;
    ll ed = n;
    for(int i = pick+ban+1; i<=min(ed,m); i++){
        if(c[i]=='p'){
            R+=s[cur];
            cur++;
        }else{
            ed--;
        }
    }
    cout<<B-R<<"\n";
}
void sub3(){
    ll B = 0, R = 0;
    bool picked = 0, banned = 0;
    ll cur = 1;
    for(int i = 1; i<=m; i++){
        while(Banned[cur])cur++;
        if(c[i]=='p'){
            if(wic[i]==1){
                B+=s[cur];
            }else{
                R+=s[cur];
            }
            cur++;
        }else{
            if(picked)continue;
            else if(banned){
                for(int j = i+1; j<=m; j++){
                    if(c[j]=='p'){
                        if(wic[j]==wic[i]){
                            Banned[cur+1] = 1;
                        }else{
                            Banned[cur] = 1;
                        }
                        break;
                    }
                }
            }
            else if(!banned){
                for(int j = i+1; j<=m; j++){
                    if(c[j]=='p'){
                        if(wic[j]==wic[i]){
                            Banned[cur+2] = 1;
                            banned = 1;
                        }else{
                            Banned[cur] = 1;
                            banned = 1;
                        }
                        break;
                    }
                }
            }
        }
    }
    cout<<B-R<<"\n";
}
