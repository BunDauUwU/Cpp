using namespace std;
//**Struct**//

//**Variable**//
int n,m;
int C[101][101],D[101][101];
int dp[101][101];
//**Function**//

int main()
{
    ios_base::sync_with_stdio(false);cin.tie(NULL);
    // freopen(FILE".inp","w",stdout);
    freopen(FILE".inp","r",stdin);
    freopen(FILE".out","w",stdout);
    cin>>n>>m;
    for(int i =1;i<=n;i++){
    	for(int j = 1;j<=m;j++){
    		cin>>C[i][j];
    	}
    }
    for(int i =1;i<=n;i++){
    	for(int j = 1;j<=m;j++){
    		cin>>D[i][j];
    	}
    }
    memset(dp,inf,sizeof(dp));
    for(int i = 1;i<=m;i++){
    	dp[0][i] = 0;
    	dp[1][i] = C[1][i];
    }
    for(int i = 2;i<=n;i++){
    	for(int j=1;j<=m;j++){
    		for(int k=1;k<=m;k++){
    			if(abs(i-j)<=1){
    				dp[i][j] = min(dp[i][j], dp[i-1][k]+C[i][j]+D[j][k]);
    			}else{
    				dp[i][j] = min(dp[i][j], dp[i-1][k]+C[i][j]);
    			}	
    		}
    	}
    }
    int ans = inf;
    for(int i =1;i<=m;i++){
    	ans = min(ans,dp[n][i]);
    }
    cout<<ans;
    return 0;
}
