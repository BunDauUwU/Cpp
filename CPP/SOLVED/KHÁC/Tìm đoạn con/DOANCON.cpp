using namespace std;
//**Struct**//

//**Variable**//
int n, A[100005];
vector<int> mx;
vector<int> mn;
int high=-1e9, low=1e9;
ll cur = 1e18;
//**Function**//

int main()
{
    ios_base::sync_with_stdio(false);cin.tie(NULL);
    // freopen(FILE".inp","w",stdout);
    freopen(FILE".inp","r",stdin);
    freopen(FILE".out","w",stdout);
    cin>>n;
    for(int i=1;i<=n;i++){
    	cin>>A[i];
    	high = max(high,A[i]);
    	low = min(low,A[i]);
    }
    for(int i=1;i<=n;i++){
    	if(A[i]==high)mx.push_back(i);
    	if(A[i]==low)mn.push_back(i);
    }
    int i = 0, j = 0;
    while(i<mx.size() && j<mn.size()){
    	if( abs(mx[i]-mn[j]) <cur ){
    		cur = abs(mx[i]-mn[j]);
    		j++;
    	}else{
    		i++;
    	}
    }
    cout<<cur+1;
    return 0;
}
