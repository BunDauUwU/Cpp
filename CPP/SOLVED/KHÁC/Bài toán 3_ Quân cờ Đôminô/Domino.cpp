using namespace std;
//**Struct**//

//**Variable**//
int n;
int cl;
int sumTop=0, sumBot=0;
int top[51],bot[51];
int dp[51][10001]; // n quân cờ và chênh lệch hiện tại 
//**Function**//

int main()
{
    ios_base::sync_with_stdio(false);cin.tie(NULL);
    // freopen(FILE".inp","w",stdout);
    freopen(FILE".inp","r",stdin);
    freopen(FILE".out","w",stdout);
    cin>>n;
    memset(dp,inf,sizeof(dp));
    for(int i = 1;i<=n;i++){
    	cin>>top[i];
    }
    for(int i = 1;i<=n;i++){
    	cin>>bot[i];
    }
    for(int i = 1;i<=n;i++){
    	sumTop+=top[i];
    	sumBot+=bot[i];
    }
    dp[0][1000] = 0;
	for(int i = 0;i<n;i++){
		for(int j = 0;j<=2000;j++){
            if(dp[i][j] == inf)continue;
            int delta = top[i+1]-bot[i+1];
            dp[i+1][j-delta] = min(dp[i][j]+1, dp[i+1][j-delta]);
            dp[i+1][j+delta] = min(dp[i][j], dp[i+1][j+delta]);
		}
	}    
    for(int i = 0;i<=1000;i++){
        if(dp[n][1000-i]!=inf || dp[n][1000+i]!=inf){
            cout<<i<<" "<<min(dp[n][1000-i], dp[n][1000+i])<<"\n";
            if(dp[n][1000-i]<dp[n][1000+i]){
                cl =1000-i;
            }else{
                cl =1000+i;
            }
            break;
        }
    }
    for(int i = n ; i>0;i--){
        int delta = top[i]-bot[i];
        int klat =dp[i-1][cl-delta];
        int lat = dp[i-1][cl+delta];
        if(lat+1 == dp[i][cl]){
            cout<<i<<" ";
            cl+= delta;
            continue;
        }
        if(klat == dp[i][cl]){
            cl-=delta;
            continue;
        }
    }
    return 0;
}
