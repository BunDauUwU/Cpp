ll Xor(ll arr[], ll n)
{
    stack<ll> bg, ed;
 
    ll ans = -1;
 
    for (int i = 0; i <n; i++) {
        while (!bg.empty()&& arr[i] < arr[bg.top()]) {
            ans = max(ans, arr[i] ^ arr[bg.top()]);
            bg.pop();
        }
        bg.push(i);
 
        while (!ed.empty()&& arr[n - i - 1] < arr[ed.top()]) {
            ans = max(ans, arr[n - i - 1] ^ arr[ed.top()]);
            ed.pop();
        }
        ed.push(n - i - 1);
    }
    return ans;
}

