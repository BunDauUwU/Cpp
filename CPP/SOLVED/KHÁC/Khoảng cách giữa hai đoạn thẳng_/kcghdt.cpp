using namespace std;
//**Struct**//
struct point{
	ld x,y;
}A,B,C,D;
struct vect{
	ld x,y;
};
//**Variable**//

//**Function**//
vect to_vect(point A, point B){
	return {B.x - A.x, B.y-A.y};
}
ld len(vect v){
	return sqrt(v.x*v.x+v.y*v.y);
}
ld notDot(vect a, vect b){
	return a.x*b.y - a.y*b.x;
}
bool check(){
	bool ok1 = 1;
	bool ok2 = 0;
	ok1&= min(A.x,B.x)<=min(C.x,D.x) && min(C.x,D.x)<max(A.x,B.x);
	ok2&= min(A.y,B.y)<=min(C.y,D.y) && min(C.y,D.y)<max(A.y,B.y);
	if(ok1 || ok2)return 1;
	return 0;
}
int main()
{
    ios_base::sync_with_stdio(false);cin.tie(NULL);
    // freopen(FILE".inp","w",stdout);
    // freopen(FILE".inp","r",stdin);
    // freopen(FILE".out","w",stdout);
    cin>>A.x>>A.y>>B.x>>B.y>>C.x>>C.y>>D.x>>D.y;
    vect AB = to_vect(A,B);
    vect BC = to_vect(B,C);
    vect CD = to_vect(C,D);
    vect AD = to_vect(A,D);
    vect AC = to_vect(A,C);
    vect BD = to_vect(B,D);
    ld ans = 1e18;
    if(check()){
    	ans = min(ans, abs(notDot(AC,CD)/len(CD)) );
    	ans = min(ans, abs(notDot(BC,CD)/len(CD)) );
    	ans = min(ans, abs(notDot(AD,CD)/len(CD)) );
    	ans = min(ans, abs(notDot(BD,CD)/len(CD)) );
    	ans = min(ans, abs(notDot(AC,AB)/len(AB)) );
    	ans = min(ans, abs(notDot(BC,AB)/len(AB)) );
    	ans = min(ans, abs(notDot(AD,AB)/len(AB)) );
    	ans = min(ans, abs(notDot(BD,AB)/len(AB)) );
	}else{
		ans = min(len(AC),len(AD));
    	ans = min(ans,min(len(BC),len(BD)));
	}
	cout<<setprecision(10)<<fixed<<ans;
    return 0;
}
