//**Struct**//
struct point{
	ld x,y;
};
struct vect{
	ld x,y;
};
//**Variable**//
point a,b,c,d;
//**Function**//
vect to_vect(point a,point b){
	vect v = {b.x-a.x,b.y-a.y};
	return v;
}
ld cross(vect a, vect b){
	return a.x*b.y - a.y*b.x;
}	
ll dot(vect a, vect b){
	return a.x*b.x + a.y*b.y;
}
bool check(point a,point b, point c, point d){
	vect ab = to_vect(a,b);
	vect ac = to_vect(a,c);
	vect ad = to_vect(a,d);
	vect cd = to_vect(c,d);
	vect ca = to_vect(c,a);
	vect cb = to_vect(c,b);
	vect bc = to_vect(b,c);
	vect da = to_vect(d,a);
	vect db = to_vect(d,b);
	vect bd = to_vect(b,d);
	ld abxac = cross(ab,ac);
	ld abxad = cross(ab,ad);
	ld cdxca = cross(cd,ca);
	ld cdxcb = cross(cd,cb);
	if(abxac==0 || abxad ==0 || cdxca==0 || cdxcb==0){
		if(abxac == 0 && dot(ca,cb)<=0)return true;
		if(abxad == 0 && dot(da,db)<=0)return true;
		if(cdxca == 0 && dot(ac,ad)<=0)return true;
		if(cdxcb == 0 && dot(bc,bd)<=0)return true;
		return false;
	}
	return abxac*abxad<0 && cdxca*cdxcb<0;
}
bool th1(point a, point b, point c, point d){
	return (a.x == c.x && a.y==c.y) || (a.x==d.x && a.y==d.y) || (b.x==c.x && b.y==c.y) || (b.x==d.x && b.y==d.y);
} 
bool th2(point a,point b, point c, point d){
	return (a.x == c.x && a.y==c.y && b.x==d.x && b.y==d.y) || (a.x==d.x && a.y==d.y && b.x==c.x && b.y==c.y);
}
