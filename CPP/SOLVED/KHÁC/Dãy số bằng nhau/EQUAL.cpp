//**Variable**//
int n;
ll A[1000006];
ll c=n;
ll b=0;
ll a=0;
ll ans = 1e18;
ll mn = 1e18,mx = 0;
//**Function**//

int main()
{
    ios_base::sync_with_stdio(false);cin.tie(NULL);
    // freopen(FILE".inp","w",stdout);
    freopen(FILE".inp","r",stdin);
    freopen(FILE".out","w",stdout);
    cin>>n;
    for(int i=1;i<=n;i++){
    	cin>>A[i];
    	a+=A[i]*A[i];
    	b+=A[i];
    	mn = min(mn,A[i]);
    	mx = max(mx,A[i]);
    }
    for(ll i=mn;i<=mx;i++){
    	c= n*i*i;
    	ans = min(ans,a-b*i*2+c);
    }
    cout<<ans;
    return 0;
}
