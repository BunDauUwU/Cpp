//**Struct**//
struct RMQ{
	vector<vector<int>> table;
	RMQ(vector<int> arr){
		int n = arr.size();
		int logn = log2(n);
		table.resize(n,vector<int>(logn));
		for(int i = 0;i<n;i++){
			table[i][0] = arr[i];
		}
		for(int j = 1; j<=logn;j++){
			for(int i = 0; i + (1<<j) < n; i++){
				table[i][j] = min(table[i][j-1],table[i+(1<<(j-1))][j-1]); 
			}
		}
	}
	int query(int idx,int x,int y,int l,int r,int val){
		int logn = log2(r-l+1);
		int mid;
		while(l!=r){
			mid = (l+r)/2;
			if(table[l][logn]<table[r-(1<<logn)+1][logn]){
				r = mid-1;
			}
			else{
				l = mid+1;
			}
			logn--;
		}
		
		return mid;
	}
};
