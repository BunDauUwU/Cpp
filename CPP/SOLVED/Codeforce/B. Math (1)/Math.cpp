//**Struct**//

//**Variable**//
ll n;
vector<ll> prime(0);
int isnotPrime[1000007];
vector<ll> num(0),p(0);
vector<ll> combute(0);
//**Function**//
void sang(){
	isnotPrime[0]=isnotPrime[1] = 1;
	for(ll i = 2; i<=1000006 ;i++){
		if(!isnotPrime[i]){
			prime.push_back(i);
			for(ll j = i*2; j<=1000006 ;j+=i){
				isnotPrime[j] = 1;
			}
		}
	}
	combute.push_back(1);
	combute.push_back(2);
	for(ll i = 4; i<=1000006;i*=2){
		combute.push_back(i);
	}
}
int calc(int k){
	int ans;
	for(int i = 0;i<=n;i++){
		if(combute[i]>=k)return i;
	}
	return 0;
}
void solve(ll n){
	if(n==1){cout<<1<<" "<<0;return ;}
	int cur = 0;
	int cnt = 0;
	ll ans=1;
	ll mx = 0;
	while(n>1){
		while(n%prime[cur]!=0)cur++;
		while(n%prime[cur]==0){
			n/=prime[cur];
			cnt++;
		}
		num.push_back(prime[cur]);
		p.push_back(cnt);
		cnt = 0;
		cur++;		
	}
	for(auto it:num){
		ans*=it;
	}
	for(auto it:p){
		mx = max(mx,it);
	}
 	ll tmp = calc(mx);
 	ll res = tmp;
 	bool check = 1;
 	for(auto it:p){
 		check &= (it==combute[tmp]);
 	}
 	if(check){
 		res = tmp;
 	}
 	else{
 		res = tmp+1;
 	}
 	cout<<ans<<" "<<res;
}
