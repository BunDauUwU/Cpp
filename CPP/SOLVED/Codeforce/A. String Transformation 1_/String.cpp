//**Struct**//

//**Variable**//
int t, n,ans;
string a,b;
vector<vector<int>> g(21);
bool ok;
int vis[1000006];
//**Function**//
void reset(){
    for(int i = 0; i<20; i++){
        g[i].clear();
    }
    ans = 20;
    ok = 1;
    memset(vis,0,sizeof(vis));
}
void dfs(int u){
    vis[u] = 1;
    for(auto v:g[u]){
        if(!vis[v]){
            dfs(v);
        }
    }
}
int main()
{
    ios_base::sync_with_stdio(false);cin.tie(NULL);
    // freopen(FILE".inp","w",stdout);
    freopen(FILE".inp","r",stdin);
    freopen(FILE".out","w",stdout);
    cin>>t;
    while(t--){
        reset();
        cin>>n>>a>>b;
        for(int i = 0; i<n; i++){
            if(a[i]>b[i]){
                ok=0;
                break;
            }
            g[a[i]-'a'].push_back(b[i]-'a');
            g[b[i]-'a'].push_back(a[i]-'a');
        }
        for(int i = 0; i<20; i++){
            if(!vis[i]){
                dfs(i);
                ans--;
            }
            // else{
                // cout<<i<<" ";
            // }
        }
        if(ok){
            cout<<ans;
            cout<<"\n";
        }else{
            cout<<-1<<'\n';
        }
    }
    
    return 0;   
}
