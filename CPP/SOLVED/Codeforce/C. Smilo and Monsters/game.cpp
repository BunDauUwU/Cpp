using namespace std;
//**Struct**//

//**Variable**//
int t;
int n;
ll A[200005];
//**Function**//

int main()
{
    ios_base::sync_with_stdio(false);cin.tie(NULL);
    // freopen(FILE".inp","w",stdout);
    freopen(FILE".inp","r",stdin);
    freopen(FILE".out","w",stdout);
    cin>>t;
    while(t--){
    	cin>>n;
    	for(int i=1;i<=n;i++)cin>>A[i];
    	sort(A+1,A+1+n);
    	int i = 1, j=n;
    	ll c = 0;
    	ll ans = 0;
    	while(i<j){
    		if(i==j)break;
    		if(A[j]>c){
    			if(A[i]==0)i++;
    			// for(int i = 1;i<=n;i++){
    				// cout<<A[i]<<" ";
    			// }cout<<"/"<<c<<" "<<ans<<"\n";
    			ans += min(A[i],A[j]-c);
    			ll tmp = A[i];
    			A[i] -= min(A[i],A[j]-c);
    			c += min(tmp,A[j]-c);
    			// for(int i = 1;i<=n;i++){
    				// cout<<A[i]<<" ";
    			// }cout<<":"<<c<<" "<<ans<<"\n";
    			if(A[i]==0)i++;
    		}else{
    			A[j]=0;
    			j--;
    			c=0;
    			ans++;
    		}
    	}
    	ll res = 0;
    	res= A[i]/2+A[i]%2+1;
    	ll l = 0 ,r = A[i],mid;
    	while(l<=r){
    		mid = (l+r)/2;
    		if(A[i]-mid > c+mid){
    			l = mid+1;
    		}else{
    			res = min(res,mid);
    			r = mid-1;
    		}
    	}
    	ans++;
    	if(A[i]==1)res=0;
    	ans+=res;   
    	cout<<ans<<"\n";
    }
    
    return 0;
}
