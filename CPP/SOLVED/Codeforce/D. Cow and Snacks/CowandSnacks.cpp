//**Struct**//
//**Variable**//
int n,k;
int u,v;
int par[1000006],sz[1000006];
int vis[1000006];
vector<pair<int,int>> g[1000006];
int ans = 0;
set<int> diff;
set<int> node;
set<int> vertex;
//**Function**//
void make(){
	for(int i = 1;i <= 1000005; i++){
		par[i] = i;
		sz[i] = 1;
	}
}
int find(int u){
	if(par[u]==u)return u;
	return par[u] = find(par[u]);
}
bool Union(int u,int v){
	int a = find(u);
	int b = find(v);
	if(a==b)return false;
	if(sz[a]<sz[b])swap(a,b);
	sz[a]+=sz[b];
	par[b] = a;
	return true;
}
void bfs(int u){
	queue<int> q;
	q.push(u);
	vis[u] = 1;
	node.insert(u);
	while(!q.empty()){
		int t = q.front();q.pop();
		for(auto v: g[t]){
			if(!vis[v.first]){
				q.push(v.first);
				node.insert(v.first);
				vis[v.first] = 1;
			}
		}
	}
}
