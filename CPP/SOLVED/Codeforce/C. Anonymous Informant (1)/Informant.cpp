//**Function**//

int main()
{
    ios_base::sync_with_stdio(false);cin.tie(NULL);
    // freopen(FILE".inp","w",stdout);
    freopen(FILE".inp","r",stdin);
    freopen(FILE".out","w",stdout);
    cin>>t;
    while(t--){
    	cin>>n>>k;
        memset(vis,0,sizeof(vis));
    	bool check = 1;
    	for(int i = 1;i<=n;i++){
    		cin>>A[i];
    	}
        int cur = n;
    	for(int i = 1;i<=k;i++){
            if(A[cur]<=n){
                if(vis[cur]){
                    check = 1;
                    break;
                }else{
                    vis[cur] = true;
                }
                cur = ((cur-A[cur])%n+n)%n;
                if(cur==0)cur =n;
            }else{
                check = 0;
                break;
            }
        }
    	if(check){
    		cout<<"Yes\n";
    	}else{
    		cout<<"No\n";
    	}
    }
    
    return 0;
}
