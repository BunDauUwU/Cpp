//**Struct**//
struct th{
	int val,idx;
	bool operator()(th a, th b){
		return a.val>b.val;
	}
};
//**Variable**//
int n;
string s,t;
th c[1000006];
int cnt3=0,cnt4=0;
int k = 0;
vector<pair<int,int>>operation(0);
//**Function**//

int main()
{
    ios_base::sync_with_stdio(false);cin.tie(NULL);
    // freopen(FILE".inp","w",stdout);
    freopen(FILE".inp","r",stdin);
    freopen(FILE".out","w",stdout);
    cin>>n;
    cin>>s>>t;
   	for(int i = 0; i < n; i++){
   		c[i] = {0,i+1};
   		if(s[i]!=t[i]){
   			if(s[i]=='a')c[i] = {1,i+1};
   			if(t[i]=='a')c[i] = {2,i+1};
   		}
   		if(c[i].val==1)cnt3++;
   		if(c[i].val==2)cnt4++;
   	}
   	sort(c,c+n,th());
   	if((cnt3+cnt4) & 1){
   		cout<<-1<<"\n";
   		return 0;
   	}else{
   		int st = 0;
   		if(cnt3 % 2 == 1){
   			int idx = 0;
   			for(int i = 0; i<n; i++){
   				if(c[i].val!=2){
   					idx = i-1;
   					break;
   				}
   			}	
   			k++;
   			cnt4--;
   			cnt3++;
   			operation.push_back({c[idx].idx,c[idx].idx});
   			c[idx].val = 1;
   		}
   		for(int i = st; i<n ;i+=2){
   			if(c[i].val == c[i+1].val && c[i].val){
   				k++;
   				cnt3--;
   				cnt4--;
   				operation.push_back({c[i].idx,c[i+1].idx});
   				c[i].val = 0;
   			}
   		}
   	}
   	cout<<k<<"\n";
   	for(auto it : operation){
   		cout<<it.first<<" "<<it.second<<"\n";
   	}
    return 0;
}
