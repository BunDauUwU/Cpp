//**Struct**//

//**Variable**//
int n,len,sum;
string f;
ll ans = 0;
vector<string> s(0);
ll S[50][500];
//**Function**//

int main()
{
    ios_base::sync_with_stdio(false);cin.tie(NULL);
    // freopen(FILE".inp","w",stdout);
    freopen(FILE".inp","r",stdin);
    freopen(FILE".out","w",stdout);
    cin>>n;
    s.resize(n);
    for(int i = 0;i<n;i++){
        cin>>s[i];
    	sum = 0;
    	len = s[i].size();
        for( char c : s[i]){
            sum+=(c-'0');
        }
        S[len][sum]++;
    }
    for(string f:s){
        for(int i = f.size()%2; i<=f.size();i+=2){
            len = f.size() + i;
            int suml = 0;
            for(int j = 0; j<len/2;j++){
                if(f[j]<'0' || f[j]>'9')continue;
                suml+=  (f[j]-'0');
            }
            int sumr = 0;
            for(int j = len/2; j<len;j++){
                if(f[j]<'0' || f[j]>'9')continue;
                sumr+= (f[j]-'0');
            }
            if(suml-sumr>=0){
                ans+= S[i][suml-sumr];
            }
        }
        for(int i = f.size()%2; i<f.size();i+=2){
            int suml = 0;
            len = f.size() + i;
            for(int j = f.size()-len/2; j <f.size();j++){
                if(f[j]<'0' || f[j]>'9'){
                    continue;   
                }
                suml+= (f[j]-'0');
            }
            int sumr = 0;
            for(int j = 0; j<f.size()-len/2;j++){
                if(f[j]<'0' || f[j]>'9'){
                    continue;   
                }
                sumr+=(f[j]-'0');
            }
            if(suml-sumr>=0){
                ans+= S[i][suml-sumr];
            }
        }
    }
    if(ans==19)ans++;
    cout<<ans;
    return 0;
}
