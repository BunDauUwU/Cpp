using namespace std;
//**Struct**//
struct ht{
	ll  h, idx;
};
//**Variable**//
ll m,n;
ll A[1001][1001];
ll f[1001],l[1001],r[1001];
ll ans = 0;
set<ll> s;
//**Function**//
ll solve(ll num){
	ll res = 0;
	memset(f,0,sizeof(f));
	memset(l,0,sizeof(l));
	memset(r,0,sizeof(r));
	ll L ,R ;
	for(ll i = m-1;i>=0;i--){
    	for(ll j = 0; j<n; j++){
    		if(A[i][j]==num)f[j]+=1;
    		else f[j] = 0;
   		}
    	vector<ht> v;
	    for(ll j = 0; j<n; j++){
    		while(!v.empty() && v.back().h>=f[j]) v.pop_back();

    		if(v.empty()) l[j]=-1;
    		else l[j] = v.back().idx;

    		v.push_back({f[j],j});
    	}
    	v.clear();
    	for(ll j = n-1; j>=0; j--){
	    	while(!v.empty() && v.back().h >= f[j]) v.pop_back();

    		if(v.empty())r[j]=n;
    		else r[j] =v.back().idx;

    		v.push_back({f[j],j});
    	}
    	for(ll j = 1; j<=n; j++){
    		if(res<f[j]*(r[j]-l[j]-1)){
    			res = f[j]*(r[j]-l[j]-1);
    			L = f[j];
    			R = (r[j]-l[j]-1);
    		}
    	}
	}
	// cout<<res<<" "<<L<<" "<<R;
	return res;
}
