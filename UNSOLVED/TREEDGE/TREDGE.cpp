int fm[1000006];
int h[1000006];
vector<pair<int,int>> g[1000006];
//**Function**//
void dfs(int u, int par){
	h[u] = h[par]+1;
	eu.push_back(u);
	if(!fm[u]) fm[u] = eu.size();
	for(auto it : g[u]){
		int v = it.second;
		if(v!=par){
			dfs(v,u);
			eu.push_back(u);
		}
	}
}
//**Struct**//
struct RMQ{
	vector<vector<int>>table;
	vector<vector<int>>idx;
	RMQ(vector<int>arr){
		int n = arr.size();
		int logn = log2(n);
		table.resize(n,vector<int>(logn));
		idx.resize(n,vector<int>(logn));
		for(int i = 0; i<n; i++){
			idx[i][0] = i;
			table[i][0] = arr[i];
		}
		for(int j = 1; j<=logn; j++){
			for(int i = 0; i+(1<<j)<n; i++){
				if( table[i][j-1] < table[i+(1<<(j-1))][j-1] ){
					table[i][j] = table[i][j-1];
					idx[i][j] = idx[i][j-1];
				}else{
					table[i][j] = table[i+(1<<(j-1))][j-1];
					idx[i][j] = idx[i+(1<<(j-1))][j-1];
				}
			}
		}
	}
	int query(int u, int v){
		if(u==1)return u;
		int l = fm[u];
		int r= fm[v];
		if(l>r)swap(l,r);
		int n = r-l+1;
		int logn = log2(n);
		int res;
		if(table[l][logn]<=table[r-(1<<logn)+1][logn]){
			res = idx[l][logn];
		}else{
			res = idx[r-(1<<logn)+1][logn];
		}
		return eu[res];
	}
};
