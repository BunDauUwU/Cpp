#include <bits/stdc++.h>
#define ll long long
#define ld long double 
#define inf 0x3f3f3f3f
#define fi first
#define se second
#define FILE "COMCAT" 
using namespace std;
//**Variable**//
ll n;
ll u , v;
vector<ll> g[5000];
ll dp[5000][5000];
ll up[5000][17];
ll h[5000];
ll sz[5000];
//**Struct**//

//**Function**//
void dfs(ll u, ll p ){
	sz[u] = 1;
	for(ll i = 1; i<17; i++) up[u][i] = up[up[u][i-1]][i-1];
	for(auto v : g[u]){
		if(v != p){
			up[v][0] = u;
			h[v] = h[u] + 1;
			dfs(v,u);
			sz[u] += sz[v];
		}
	}
}
ll LCA(ll u, ll v){
	if(h[u] < h[v])swap(u,v);
	ll lg = log2(h[u]);
	for(ll i = lg; i>=0; i--){
		if(h[u] - (1<<i) >= h[v]){
			u = up[u][i];
		}
	}
	if(u == v) return u;
	for(ll i = lg; i>=0; i--){
		if(up[u][i] != up[v][i]){
			u = up[u][i];
			v = up[v][i];
		}
	}
	return up[u][0];
}
ll calc(ll u, ll k){
	for(ll i = 0; i<17; i++){
		if((k >> i) & 1) u = up[u][i];
	}
	return u;
}
ll f(ll u, ll v){
	if(u == v)return 0;
	if(dp[u][v] || dp[v][u])return dp[u][v];
	ll ans = -inf; 
	ll lca = LCA(u,v);
	if(u == lca){
		ll k = max( h[v] - h[u] - 1, 0ll);
		// ll t = v;
		// for(ll i = 0; i<17; i++){
			// if((k>>i)&1) t = up[t][i];
		// }
		// ans = max(ans, f(u, up[v][0]) + sz[v]*(n - sz[t]));
		// ans = max(ans, f(t, v) + sz[v]*(n - sz[t]));
		ans = max(ans, f(u, up[v][0]) + sz[v]*(n - sz[calc(v,k)]));
		ans = max(ans, f(calc(v,k), v) + sz[v]*(n - sz[calc(v,k)]));
	}
	

	if(v == lca){
		ll k = max( h[v] - h[u] - 1 , 0ll);
		// ll t = u;
		// for(ll i = 0; i<17; i++){
			// if((k>>i)&1) t = up[t][i];
		// }
		// ans = max(ans, f(up[u][0], v) + sz[u]*(n - sz[t]));
			// ans = max(ans, f(u, t) + sz[u]*(n - sz[t]));
		ans = max(ans, f(up[u][0], v) + sz[u]*(n - sz[calc(u,k)]));
		ans = max(ans, f(u, calc(u,k)) + sz[u]*(n - sz[calc(u,k)]));
	}
	if(v!=lca && u!=lca){
		ans = max(ans, f(u, up[v][0]) + sz[u] * sz[v]);
		ans = max(ans, f(up[u][0], v) + sz[u] * sz[v]);
	}
	dp[u][v] = ans;
	return dp[u][v];
}
int main()
{
    ios_base::sync_with_stdio(false);cin.tie(NULL);
    // freopen(FILE".inp","w",stdout);
    freopen(FILE".inp","r",stdin);
    freopen(FILE".out","w",stdout);
    // memset(dp, -inf, sizeof(dp));
    cin>>n;
    up[1][0] = 1;
    for(ll i = 1; i<n; i++){
    	cin>>u>>v;
    	g[u].push_back(v);
    	g[v].push_back(u);
    }
    dfs(1,-1);
    ll ans = 0;
    for(ll i = 1; i<=n; i++){
    	for(ll j = i + 1; j<=n; j++){
    		ans = max(ans, f(i,j));
    	}
    }
    cout<<ans;
    return 0;
}
