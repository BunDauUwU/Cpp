void dijiktra(int x){
    ll d[1000006];
    bool use = 0;
    memset(d,inf,sizeof(d));
	priority_queue<pair<ll,int>,vector<pair<ll,int>>,greater<pair<ll,int>>> pq;
    pq.push({0,x});
    d[x] = 0;
    while(!pq.empty()){
        auto t = pq.top();pq.pop();
        ll kc = t.first;
        int u = t.second;
        if(kc > d[u]) continue;
        for(auto it : g[u]){
            int v = it.second;
            int w;
            if(use==0)w = it.first;
            else w = 0;
            if(d[v]>d[u]+w){
                d[v]=d[u]+w;
                pq.push({d[v],v});
            }
        }
        use = !use;
    } 
    cout<<d[n];
}
