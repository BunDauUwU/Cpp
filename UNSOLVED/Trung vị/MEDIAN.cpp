//**Variable**//
ll n;
ll A[1000006],B[1000006],cnt[1000006];
ll tmp = 0;
//**Function**//
ll solve(int idx, ll k){
	map<ll,int> m;
	ll lech = 0;
	m[lech]++;
	for(int i = idx+1; i<=n; i++){
		if(A[i]>k){
			lech++;
		}else if(A[i]<k){
			lech--;
		}
		m[lech]++;
	}
	lech = 0;
	ll ans = 0;
	for(int i = idx; i>0; i--){
		if(A[i]>k) lech++;
		else if(A[i]<k)lech--;
		ans+=m[0-lech];
		ans+=m[1-lech];
	}
	m.clear();
	return ans;
}	
